<H4>The Details</H4>
I give small "thank you" goodies to players who donated, in the form of
a "Sir" or "Lady" title for your characters, and the ability to play one
more character than other players can.<br>
These goodies last for one week for every full Euro that arrives in my
account (please read the <a href="#fineprint">fineprint</a>!).<br>
Whatever you want to donate is fine with me. Just choose any amount you
feel comfortable with.<br>
<p>At this time, I can only offer PayPal and money transfers. For small
sites like this it is very difficult to find payment services that don't
ask for high monthly fees, which would defeat the purpose of a donation
system.<br>
If you live in Germany or can make direct bank transactions to accounts
with the german Postbank, and would prefer that, please contact me
directly for bank details.<br>
<p>What is happening with the money? For one, it pays for the hosting fees
that the server this all runs on costs. Donations have also paid for a new server, memory
upgrades and other hardware, as well as a week of unpaid leave for me
which was spent coding new features into the game.<br>
Most of what's left simply goes into my pockets, as a small compensation 
for the many hundreds of hours I put into the game.
