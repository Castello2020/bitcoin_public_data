          <h2 class="titleseparator">Features</h2>
          <ul class="squarebullets">
            <li><b>Readout of tags</b> - Playlist Creator can read the tags of all of your media files and use the data for customizing the playlist.</li>
            <li><b>High processing speed</b> - Very high processing speed even with activated readout of tags, thanks to the support of the powerful open source library <a href="http://mediainfo.sourceforge.net" title="Goto MediaInfo website">MediaInfo</a>.</li>
            <li><b>Drag and Drop</b> - Add your music files with style by using drag and drop from Windows Explorer.</li>
            <li><b>Recursive folder adding</b> - Folders can be added recursively, i.e. the folder and all its subfolders. Enables you to add your entire music collection via a single drag and drop action.</li>
            <li><b>Dynamic Titles</b> - Define dynamic title variables in order to use tag data for customizing the playlist output.</li>
            <li><b>Absolute / Relative</b> - The playlist items can be saved absolute or relative to the save location of the playlist.</li>
            <li><b>Many predefined file types</b> - There are a lot of predefined file types which can instantly be added to the playlist.</li>
            <li><b>Custom file types</b> - If you think there is an important file type missing you can easily add it to the list of accepted file types.</li>
            <li><b>PLS and M3U playlists</b> - For compatibility with a wide variety of hardware and software Playlist Creator can create playlists both in PLS- and M3U-format.</li>
            <li><b>Open or insert playlists</b> - Open existing playlist files to modify them or insert playlist files into your current playlist.</li>
            <li><b>Shuffle playlist</b> - If you like variation the playlist can be shuffled with a simple click.</li>
            <li><b>Always up-to-date</b> - The application can check (automatically) for newer versions.</li>
          </ul>
          
