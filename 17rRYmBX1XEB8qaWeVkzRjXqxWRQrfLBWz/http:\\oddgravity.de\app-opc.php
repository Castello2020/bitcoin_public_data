          <h1 class="titleseparator">Playlist Creator 3.6.2</h1>
          <div class="r-image"><img src="images/opc-screen.png" alt="Playlist Creator" width="240" height="253" /></div>
          <p>Following the Unix philosophy "one tool for one job" Playlist Creator enables you to create playlists of your precious music within seconds.</p>
          <p>The composition and creation of a playlist is quite intuitive: Add all desired files, enter a name for the playlist and select its save location. Hit the create button and one moment later your brand-new playlist is ready.</p>
          <p>You don't have to recreate your playlist every time you want to make changes to it. Just open the existing playlist file, make the changes you want and save the playlist. It's as simple as that!</p>
          <p>Combining several playlists into a single big playlist is quite simple as well: Insert as many existing playlist files to your current playlist as you like and save the playlist. Done!</p>
          
