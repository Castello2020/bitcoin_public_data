<p class="center" />
<b class="title">On Standards</b><br />
J. Morse Loyola, 24 January 2003

<p class="footnote" />
<i>Note: some of the specific details mentioned in this article are now
obsolete; for example, AOL Time Warner is now just Time Warner; as well, these
pages now validate as XHTML 1.0, not HTML 4.01, and the GIF patent finally
expired, removing that legal roadblock to using GIF images. I don't feel that
these changes detract from the general idea discussed however, so with the
exception of the validation link I'm presenting this article as it appeared on
the old site. -- /i.</i>
<p />
There are three ways to create web pages on this here World Wide Web, and
each of them has their limitations. They are, in order of popularity:
<p />
<ol>
  <li>It Looks Fine On My Monitor</li>
  <li>You Need Hypertext Transponder Version 3.14159 Build 666 To View This Site</li>
  <li>This Site Is So Standards Compliant It's Being Reccomended As An RFC</li>
</ol>
<p />
The first way listed here is, unfortunately, the most common. Its adherents
don't bother to learn how to do proper markup, because they have
pretty little drool-proof tools to do it for them. The more egregious
offenders will even have "C:\My Documents\Stupid Image.jpg" as an argument
to an IMG tag; yes, I've seen this even on commercial sites. Granted, no one
can check their work with <i>every</i> browser out there; after all, when
was the last time you fired up charlotte on OpenVMS? Still, I have to wonder
- is basic competence too much to ask? The answer to that ought to be 
obvious to anyone who's voted recently.
<p />
A subset of the WYSIWYG jockeys, however, is arguably more annoying: the Browser
Requirement people. Here we've built a scalable, public, worldwide network
accessible from any kind of hardware with a TCP/IP stack, and some lazy,
ignorant tosswit is going to demand that I run the latest Wintel virusware
in some godawful resolution just to read whatever snivelling adolescent
backwash they could be bothered to mash together? I don't fucking think so,
boys and girls. As has been <a
href="http://www.eyrie.org/~eagle/writing/rant.html">said before</a>, "...by
God I KNOW what this network is for, and you can't have it".
<p />
Which brings us nicely to number 3, the unbearable pedants. These folk are
easy to spot, with their chest-banging braggartry and macho posturing:
you'll find link buttons all over the place pointing you to the <a
href="http://www.w3c.org/">W3 Consortium's</a> <a
href="http://validator.w3c.org/">HTML Validator</a>; no doubt the authors
will even have a note somewhere talking about why the site doesn't display
properly in AOL Time Warner's Netscape Communications Corporation's
Communicator 4 Suite. What's even more frustrating about the Standards
Pedants is that (repeat after me, class) THEY ARE RIGHT.
<p />
I'm sure you can see where I'm going with this, so we'll start off with a
demonstration. Let's try to display a <a
href="http://www.libpng.org/pub/png/">PNG</a> image, shall we? Seems simple
enough, or so you'd think.
<p />
What it should look like:<br />

<img src="/img/logos/viopac_6_logo.jpg" border="0" alt="How our old logo should look" />
<p />
AOL Time Warner's Netscape Navigator 4:<br />
<img src="/img/logos/viopac_6_logo_netscape_4.jpg" border="0" alt="The old VioPac Logo as rendered by NN4" />
<p />
Your browser:<br />
<img src="/img/logos/viopac_6_logo.png" border="0" alt="What it looks like in your browser" /><p />
What's the deal with that?! You'd think the damn thing could display a
simple image correctly, wouldn't you. Well, guess what--Navigator
4.7<a href="#1"><sup>1</sup></a> and higher can't seem
to display the black pixels in a PNG graphic. Not only that, but earlier
releases couldn't even get that far (which you'll notice if you use, say,
Navigator 4.0). And we're not even going to talk about transparency; support
for that is so far beyond fucked up it's not even worth a demonstration.
<p />
Why not just use GIFs, then? Why bother with this "PNG" thing at all, when a
bright, shining, and established standard <i>already</i> exists--GIF! That
answer is simple: I can't afford to. Unisys, who own the GIF patent, have
been demanding that sites that use GIF images <a
href="http://news.com.com/2100-1023-239414.html">pay them</a> licensing
fees. See what happens when you try to create standards based on non-free
code? PNG is an established standard that is not based on code that could
get you sued, so that's what we use. For more info on the debate, have a
look at <a href="http://burnallgifs.org/">burnallgifs.org</a>.
<p />
But wait, there's more! Another WWW standard is Cascading Style Sheets, or
CSS<sup><a href="#2">2</a></sup>. CSS is a way of
assigning the same value to a tag--a color, for example--across an entire web
site. This makes it orders of magnitude easier for a designer to change,
say, the background color on all the different pages. Instead of making the
change on every page, he or she just has to do it once, and the change is
applied to every page on the site. Sounds great, don't it?
<p />
Of course, neither AOL Time Warner's Netscape Communications Division nor
Microsoft Corporation got that right. They both tried to extend control over
it in an attempt to lock out the competition, and to a degree they were both
successful: they just about managed to lock us out of the very same system
we built for them.  Microsoft Corporation may have won the browser war, but
the rest of us were the real losers.
<p />
With that in mind, our stance on the Usability vs. Standards debate is that
we will do what we can to make things show up properly in your browser, but
we have to draw the line somewhere. Mind you, we don't require any browser
more powerful than telnet to read the <i>content</i> of the site, but some
of the more flashy bloat--like images--might look a little strange in
older browsers. We are left with no other reccomendation than that the time
has finally come to put the generation 4 browsers to rest, bite the bullet,
and <a href="http://www.mozilla.org/">upgrade</a>.
