<br />
SquirrelMail is currently celebrating 10 years of providing free, Open Source Software to the world. We have a lot to be grateful for and many people to thank for how successful we've been! SquirrelMail is included in most major Linux distributions and is independently downloaded by tens of thousands of people every month, which might even make it more widespread than some of the biggest free webmail providers. And, proving that Open Source really works, our popularity has allowed us to collaborate with countless talented individuals who have helped us keep SquirrelMail safe, up to date and full of new features.
<br /><br />
But fame has its price. &nbsp; ;-) &nbsp; SquirrelMail has always been run by a small group of volunteers, and we've never been paid for our efforts. We do it because this is our passion; we do it for the love and the fun. But running a high-profile project means that there is a plethora of work from which no one gets much fun (or love). That's taken its toll -- our numbers have dwindled and our visionary initiatives for our next big release have had to take a back seat to ongoing maintenance, such as responding to bug reports, looking over plugin and patch submissions, fixing security vulnerabilities, answering voluminous amounts of email on our mailing lists, and a slew of other responsibilities. Oh, and then there's paying the bills, family time, and cleaning the kitchen....
<br /><br />
So it's time to ask our community to give back just a little bit. In the best scenario, SquirrelMail should be able to support one or two people working full time. To some, this may seem overly ambitious, but we don't think so. Think of the good money that has been raised by projects that can be considered far less mission-critical than an email system. We don't think any other projects are less deserving than SquirrelMail; only that SquirrelMail is quite worthy of your support, especially after 10 years of doing it all gratis. With support on a significant scale, the project will regain momentum, that "Web 2.0" fully-skinable release will be out in no time, and the squirrels will feel appreciated!
<br /><br />
We'd like to have you show your support by simply donating whatever you feel you can afford. Currently, you can choose to donate to any of us individually (if you have stipulations about how the money should be used/shared, let us know and we will be happy to oblige). We are also trying out a primitive bounty system, in case you want to target your money for a specific feature or improvement. If you need a US <b>tax exemption</b> for your donation, please contact us directly.
<br /><br />
As always, we'd like to encourage you to use our <a href="http://squirrelmail.org/wiki/OfficialSupport">official SquirrelMail support</a>, and if you're hiring for work projects you should consider our professional, dependable team members, since some of them make a living as consultants.
