<h2>Welcome to Higher Intellect</h2>
<p>Higher Intellect is a World Wide Web server hosting a searchable database of over 750,000 text files on a variety of subjects. We also host a server running on the Hotline and KDX protocols featuring a vast collection of antique software, obscure operating systems, and open source software. To connect, simply download a copy of <a href="gethotlinekdx.php">Hotline/KDX</a> and connect to <b>kdx.preterhuman.net</b></p>
<p style="color:#999999">"The origin of action - its effecient, not its final cause - is choice, and that of choice is desire and reasoning with a view to an end. This is why choice cannot exist either without thought and intellect or without a moral state; for good action and its opposite cannot exist without a combination of intellect and character. Intellect itself, however, moves nothing, but only the intellect which aims at an end and is practical."
- Aristotle </p>
<br/>
<p>
<h2>Latest News</h2>
<p><b>November 2013 - </b>Some changes to the archive are being made as we are locating and expanding any compressed files which contain documents or other content. This will allow for easier access to them as they will be indexed by search engines. We have also added a Bitcoin donation link for anyone wishing to assist the project.</p>
<p><b>March 2013 - </b>We are actively working to improve our content delivery, and have since deployed a total of four global mirrors to provide more than enough available bandwidth. Work is also being done to add items to the archive and allow search engines to index the collection faster.</p>
<p><b>January 2013 - </b>We are excited to announce the creation of a new content delivery network to serve the Higher Intellect text archive! Our first global mirror is now live, and all traffic to the archive is now being redirected to this faster network. You will notice an improvement in overall download speeds when accessing the texts area.</p>
<p><b>October 2012 - </b>In an effort to upgrade our infrastructure for the future, we have deployed a new primary node based on newer quad-core CPUs and VMware virtualization. The text archive and Hotline/KDX files are now stored on a RAID1 FreeNAS based SAN appliance with ZFS volumes. Aside from some future RAM upgrade requirements, the site should be stable on this new solution.</p>
