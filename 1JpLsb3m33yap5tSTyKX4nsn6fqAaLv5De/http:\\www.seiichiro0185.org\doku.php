<h1><a name="update_to_the_mailserver_post" id="update_to_the_mailserver_post" href="/blog:2013:11:update_to_the_mailserver_post">Update to the Mailserver post</a></h1>
<div class="level1">

<p>
I updated the post about my <a href="/blog:2011:01:my_mailserver_setup_postfix_dspam_dovecot" class="wikilink1" title="blog:2011:01:my_mailserver_setup_postfix_dspam_dovecot">Mailserver setup</a> today. I eliminated an extra pass of the mail through postfix after spam filtering. Dspam now directly delivers the mail to dovecot for filtering and putting it in the users mailbox.
