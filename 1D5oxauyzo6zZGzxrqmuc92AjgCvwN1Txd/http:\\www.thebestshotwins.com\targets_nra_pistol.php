	<h1>NRA Style Pistol Targets:</h1>

	<p>If you plan to earn <b>Pro-Marksman</b> all the way up to <b>Distinguished Expert</b> you should also plan to use a lot of targets!  One way to save a few dollars is to use non-qualifying targets until your skill level reaches the scores necessary for the qualification level you are trying to achieve.&nbsp; In other words if your scores are not yet high enough to achieve <b>Marksman First Class</b> then you can use the non-qualifying targets below until they are.&nbsp; Then you can switch over to your official qualifying targets to complete the <b>Marksman First Class</b> level.</p>

	<p> The pistol targets below are recreations of the official NRA targets used for the various courses of fire listed in the <b>Winchester/NRA Marksmanship Qualification Program</b> handbook.&nbsp; Once you review the handbook the target choices below will make more sense.</p>

	<h2>Handbook</h2>

	<img src="/content/downloads/Marksman_Qualification_Program_Thumb.png" alt="Thumbnail of NRA Marksman" width="125" height="160" />
	<br>
	<a class="thick" href="/content/downloads/Marksman_Qualification_Program.pdf">Winchester-NRA Marksman Qualification Program.pdf</a>

	 <p>Several courses of fire allow you to pick <b>ONE</b> of several targets available depending on your shooting circumstances.&nbsp; For example:  In the <b>Pistol Qualification</b> course you can choose to use the AP-1 target shot from 30 feet or the AP-2 target shot from 15 feet.&nbsp;  The AP-1 target is larger but further away and the AP-2 target is smaller but closer.&nbsp;  The two targets are designed to give the shooter the same <b>"sight picture"</b> regardless of the difference in shooting distance.&nbsp; Before you start shooting for your first qualification you will need to decide which target best suits your shooting environment and then stick with the same target throughout the qualification process for a particular course of fire.&nbsp;  All targets can be shot indoors or outdoors for qualification purposes.&nbsp; So, if your goal is to complete the <b>Pistol Qualification</b> course of fire and you know that you will be shooting at a very short indoor range then you will want to use the AP-2 target.&nbsp; If distance or range setup is not an issue then you may want to use the AP-1 target.&nbsp; It's your choice.&nbsp; The main point to remember is that once you pick your target you need to stick with it throughout the entire course that uses that particular target.</p>

	<p><b>The <font color="#ff0000">Best</font> Shot Wins</b> has created the below set of practice targets because they are small enough to be printed on a standard size sheet of printer paper.&nbsp; They are the same size as the official targets but can only be used for practice.&nbsp; Targets larger than a single piece of paper are not listed.&nbsp; Those targets can be purchased through various online vendors who sell official NRA Targets.&nbsp; The AP-1 target, for example, is not listed because it is too big to fit on a single sheet of paper.</p>

	<p>The 9 inch disk targets below can be used as a qualifying target for the first part of the <b>Pistol Qualification</b> course so long as all of your shots are within the margin stated in the rules.&nbsp; <a class="thick" href="/content/downloads/Marksman_Qualification_Program.pdf">(See Handbook)</a></p>

	<p>The remaining targets are <b>NOT</b> official qualifying targets and should only be used for practice when you are not scoring high enough to warrant using a qualifying target.&nbsp; They can also be used when you run out of qualifying targets but want to continue practicing while waiting for your next target order to arrive.&nbsp; It's just our way of trying to help you save a few bucks!</p>

