<div class="article">On Saturday, Oct. 12th, 2013 Pete Eyre was detained by San Francisco Police Department employees Hamdy G Habib #2329, D Khuu #358, and J Woo, transported to their outfit's facility on Mission St. for identification, and given a ransom note.

The rationale given by the aggressors to Eyre, was that he had stepped into "a lane of traffic." Yet as Eyre pointed out, his actions caused no victim. Yet the claim unthinkingly put-forth by those wearing badges was "just doing my job" - an excuse that should never be acceptable for anyone, anywhere.

Consider this situation absent the costumes worn by the actors. It's clear to see just who are the aggressors. Badges don't grant extra rights.<strong></strong>
