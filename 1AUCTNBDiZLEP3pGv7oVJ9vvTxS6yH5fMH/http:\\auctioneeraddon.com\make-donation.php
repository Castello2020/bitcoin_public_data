			alert("Please enter an amount over $2. Amounts under this get gobbled up with fees, and we'd prefer not to feed the PayPal monster.");
			return;
		}
		go(amt);
	});
	$('a.donate-other').on('click.more', function(e) {
		e.preventDefault();
