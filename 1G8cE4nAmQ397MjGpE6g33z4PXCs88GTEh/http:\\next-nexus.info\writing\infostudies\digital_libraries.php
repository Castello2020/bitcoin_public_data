<h1>What's a digital library?</h1>
<p class="ednote">Originally written in March 2012, this piece has been modified,
with  information and additional references added. As well as the references, please also see the <a href="#addpotrel">additional potentially relevant links</a> below.</p>

<h2>Introduction</h2>
<p>This piece will
explore the concept of 'digital libraries', and the collections and
services of digital libraries, in comparison to 'traditional
libraries'.<ins><a href="#tradlibnote" id="tradlibnoteanc" class="sdfootnoteanc"><sup>*</sup></a></ins>
</p>
<p>A quick exploration
of what a library is, what some services provided by libraries
include, and what a traditional library is, will be given. Then a
brief history of the idea of digital libraries, and a discussion
about what a digital library <em>is</em>, before a definition is given. The remainder of the piece will
examine the differences between digital libraries and traditional
libraries with regard collections and services offered. 
</p>
<p>The piece concludes
that the differences are mainly to do with technology, rather than a
fundamental change in the way libraries operate. This is because
digital libraries are still libraries with the same goals and
purposes.</p>
<h2>Libraries and traditional libraries</h2>
<p>Any definition of
'digital library' or 'traditional library' rests upon the definition
of 'library'. There are many different types of library, targeting
many different audiences, and they provide many different services.
For the purposes of this paper, at least, a library is: “a
<em>curated</em><A CLASS="sdfootnoteanc" NAME="sdfootnote1anc" HREF="#sdfootnote1sym"><SUP>1</SUP></A> <A CLASS="sdfootnoteanc" NAME="sdfootnote2anc" HREF="#sdfootnote2sym"><SUP>2</SUP></A>
collection of information sources<A CLASS="sdfootnoteanc" NAME="sdfootnote3anc" HREF="#sdfootnote3sym"><SUP>3</SUP></A>
intended for use <A CLASS="sdfootnoteanc" NAME="sdfootnote4anc" HREF="#sdfootnote4sym"><SUP>4</SUP></A>”<A CLASS="sdfootnoteanc" NAME="sdfootnote5anc" HREF="#sdfootnote5sym"><SUP>5</SUP></A>.
This definition does not talk about a particular group or community
(unlike definitions given by, for example Keller, Reich, &amp;
Herkovic (2003) or Lagoze, Kraft, Payette, &amp; Jesuroga (2005)). 
The central purpose of a library is to provide access to information
 Buckland,  (1997, p. Introduction). Harris (no relation to me) (1995, p. 3)
provides the following similar definition: “a library is a
collection of graphic materials arranged for relatively easy use,
cared for by an individual or individuals familiar with that
arrangement, and accessible to at least a limited number of persons”.
I read this after I originally wrote the work, but consider it quite
a nice definition. Harris also does not require a skilled
individually, merely an individual, and appears to consider the only
requirement for the library is providing access to material, not
reference work or similar.</p>
<p>Given
what a library is, a couple of points about what a library is not.
Because a library collection is curated, a random<A CLASS="sdfootnoteanc" NAME="sdfootnote6anc" HREF="#sdfootnote6sym"><SUP>6</SUP></A>
collection of material is not a library.<A CLASS="sdfootnoteanc" NAME="sdfootnote7anc" HREF="#sdfootnote7sym"><SUP>7</SUP></A>
A museum or an archive is also not a library, as the material and
resources in these places are not generally intended to be used as
such (but rather conserved). Moreover, a librarian or other
'information professional' is not required for a library to exist,
the curator could be a layperson. (While someone with training may
have a better understanding of how to curate, than a person without
training, it is not required.) 
</p>
<p>The definition of 'library' given above does not include any reference to
the services that libraries typically provide. This is because it may
be the only service that a particular library provides is access to
information. Other sorts of services depend on the type of, and
specific, library, but may include reference services
(“interpretation and discovery information for patrons”),
distribution (interlibrary loans and document delivery),
preservation, and similar  (Keller
et al., 2003).</p>
<div id="bookshops" class="aside aside1">
<h3>Bookshops</H3>
<p>Given the definition
above, could it be said that a bookshop could be a library? No, they
are different, though not for the reason that many people seem to
suspect; that at a bookshop you have to pay. After all, there exist
many libraries (private) that you have to pay to be a member of, and
some bookshops allow you to sit and read all day without paying.
Also, not all libraries allow you to take items home, so that can't
be the difference. I would argue that the difference is purpose. The
purpose of a library is to provide material to <em>use</em>, whereas
the purpose of a bookshop (or any shop) is to <em>sell</em> that
material. And of course, a better differentiation would be between
any sort of 'information' shop (including online, 'ebook sellers')
and a library. So again, an 'information' shop <em>sells</em> 
information, and exists primarily for this purpose, and a library
<em>provides access</em>  to
information <em>for usage</em>.
The bookshop does not care if once you have bought a book, you take
your book home and turn it into mulch. They have your money.</p>
</div>
<h3>traditional libraries</H3>
<p>What is a 'traditional' library? Libraries have always been centred in a
<em>physical</em>  space with
physical collections. While the nature of the collections have
changed (clay tablets, paper items (books etc.), through to
microfilm, tapes, and CDs), they have still remained located in space
(Troll, 2002).  However, since
the advent of modern ICT, libraries started offering services via
computer; some of which do not require physical access to the
library. These services included not just non-essential, but
‘nice-to-haves’ (like the catalogue becoming online and publicly
accessible), but digital <em>resources</em> ,
such as bibliographic databases, first available on CD-ROM and then
online, and then full-text databases, electronic journals and so on.
This is when 'traditional' libraries started becoming 'digital'. 
</p>
<h2>History and definition of digital libraries</h2>
<p>The idea of
electronic, interconnected, digital, virtual libraries have been
around for many years. Lynch (2005) marks 1965 as a potential turning
point between the pre-history (ideas such as Bush's (1945) Memex) and
history of digital libraries. Lynch (2005) also mentions that there
were digital libraries in the mid-1980's (“at least by some
definitions”). Harter (1997) mentions that speculative authors were
the first to write about interconected computerised libraries.<A CLASS="sdfootnoteanc" NAME="sdfootnote8anc" HREF="#sdfootnote8sym"><SUP>8</SUP></A> The earliest usage found for the term 'digital library' was in
1988 by Kahn &amp; Cerf, and slightly later, workshops were based
around the term in the early 1990s (Fox, 1993). 
</p>
<p>Since the early
1990s there has been much debate about what a digital library is.
Harter, (1997) outlines three broad view points from the most narrow,
a redefining of the 'traditional library' in digital terms, to the
broadest – the Internet is a library, with none or few of the
properties associated with traditional libraries (such as
organisation, selection, etc.). 
</p>
<p>A number of definitions (including from Seadle &amp; Greifeneder (2007) and
Trivedi (2010)) make the point that 'digital libraries' are
libraries. That  is the
“traditional library institutional missions … must extend to the
digital library environment” (Lynch &amp; Garcia-Molina, 1995) 
and that they have “ the
same purposes, functions, and goals as traditional libraries”
(Cleveland, 1998; Wainwright, 1996). 
Buckland (1997) would agree, and wrote that the same existing and
familiar principles should be applied in ways “appropriate to the
technical characteristics of the medium”. Harter (1997) argue s
that the usage of the word library should mean that 'digital library'
is a narrow term. Buckland, (1997) uses the term 'electronic library'
to mean a similar thing, where the collection is stored
electronically. Ogunsola's (2011) definition includes
that the digital resources can be stored in “various locations”,
and “accessed and used with great ease”, for multiple purposes.
Peterson (2001) makes the point that the digital library's most
important asset, and what makes it a library, is the people who give
the library focus. It is the people who select and organise the collection. 
</p>
<p>It makes little
point to call something a library if it does not have the qualities
of the library (if your aim is clear communication, at least). So,
this piece will use the term 'digital library' as if they are
libraries. Therefore the only thing needed to define is the digital
part of the term. 
</p>
<p>In the context of
the idea of a 'digital library', 'digital' means 'digital
technology'. That is, modern information communication technology
(ICT). This includes computers, computer networks (including the
Internet), mobile phones and other interconnected devices. This
meaning comes from the meaning of 'discrete', in contrast to
analogue. The binary system that makes up modern ICT is a discrete
system. 
</p>
<p>A
digital library, then, is a library where the collection and services
are in a digital form and/or accessed digitally. The collection can
be either 'born digital' (created in a digital form originally), or
scanned or otherwise copied from a physical artefact. 
</p>
<div class="aside aside1" id="autohybr">
<h3>Automatic and hybrid libraries</H3>
<p>It
makes little sense for a solely digital library to not also be a
solely virtual library<A CLASS="sdfootnoteanc" NAME="sdfootnote9anc" HREF="#sdfootnote9sym"><SUP>9</SUP></A>. As the imposition of space required for the physical
aspects may as well be used for physical items (such as newspapers
and magazines) for loan or use as well. Today, most 'traditional'
libraries are actually hybrids, a mix of both a physical collection
and a digital collection. 
Rusbridge, (1998),
Pinfield et al., (1998), and then Schwartz, (2000) discusses hybrids
in more detail. The term  'holistic'
is also used (Niegaard, 2011).
Lynch &amp; Garcia-Molina (1995) talk of digital libraries as
spanning both “print and digital materials”, in the same sense
that 'hybrid' is used here. 
</p>
<p>Buckland
(1997, p. The Automated Library) also discusses 'automated
libraries', which, have a physical collection (rather than digital),
but use modern ICT for technical operations and <em>procedural</em> 
purposes,  to facilitate
running and using the library. This usage of technology does not,
however, define the library. Ways in which technology maybe used
include: record keeping; providing an OPAC (online publicly
accessible catalogue); and sending emails and mobile phone text
messages relating to loans and due dates.</p>
</div>
<div class="aside aside2" id="digvirelec">
<h3>Digital, virtual, electronic, oh my!</H3>
<p>Tennant (1999) makes a distinction
between digital libraries, virtual libraries and electronic
libraries. However, I would suggest that this distinction is not
meaningful most of the time. 
</p>
<p>While it is true that libraries can
have 'electronic' media (including cassette tapes, CD-ROMs, DVDs,
etc.) without being a digital library, these items can be used or
loaned the same as print media and microfilm. No actual electricity
is required for the storage, or loan of these items (though
admittedly it is required for the use of them). 
</p>
<p>It is also possible to have a digital
library where the collection is stored on CD and not accessible by  a
network (though this would be disputed by some). So, it is possible
to distinguish between a digital and a virtual library; but I would
say it makes more sense to distinguish between virtual libraries and
hybrid libraries (libraries with both digital and physical
collections). There is little point in having a digital-only library
that is not also a virtual one (at least from the perspective of the
users). A digital library that does not offer remote (i.e. virtual)
access to it's holdings is a lot less useful than one that does.</p>
</div>
<h2>Differences</h2>
<p>There are a number
of differences in how a traditional library operates, compared to a
digital one. These tend to revolve around the differences in the
inherent properties of a digital collection compared to a physical
collection, and licensing issues when it comes to digital
collections.</p>
<p>With a 'traditional'
physical collection, the inherent properties of the collection hold.
It both takes up space, and so you can not have more collection than
you do space, and it is difficult for more than one person to use an
item at a time (Buckland, 1997, p. The Paper Library). If the
collection gets too big for the space, it will need to be weeded.
However, with a digital collection, the major restrictions are
artificial, relating to licensing, copyright and digital restrictions
management (DRM).<A CLASS="sdfootnoteanc" NAME="sdfootnote10anc" HREF="#sdfootnote10sym"><SUP>10</SUP></A></p>
<p>In
'traditional' libraries, material is bought, and then can be lent out
without obtaining permission from the publisher, author or other
potential copyright owner.<A CLASS="sdfootnoteanc" NAME="sdfootnote11anc" HREF="#sdfootnote11sym"><SUP>11</SUP></A>
However, with digital content, it is often not sold, but only
'licensed' (see, for example, Kahn &amp; Cerf (1988), Samuelson
(1995) and Schwartz (2000) for discussions). 
</p>
<p>Reference services
(finding information and providing answers) can be done via email,
telephone and online chat (Troll, 2002). But the person who is
answering emails may well be sitting in a public space ready to
answer face to face queries. Digital libraries may not even need
humans to answer many questions that may be asked. Software agents,
both proactive and passive (for example, the 'knowbots' of Kahn &amp;
Cerf (1988), any of a myriad of SF (science fiction/speculative
fiction) artificial intelligences, or just 'simple' search engines
allowing direct search (Schwartz, 2000)) may well be all that is
needed. 
</p>
<p>Relating to
searching, if a document or work is in the catalogue, it should be
available (assuming that it is not license restricted, and that it is
not in a location that has since disappeared (part of the problem
with cataloguing the WWW)) (Troll, 2002). That is, the catalogue
should link directly to the work (Buckland, 1997, p. Collections
Reconsidered). Buckland (1997, p. The Electronic Library) likens this
to changing from closed library stacks to open stacks in a physical
library; in the digital library: “library staff would be mainly
concerned with creating and sustaining the system so that users could
serve themselves.” .</p>
<p>Digital libraries
potentially offer greater accessibility to users who may have
previously been restricted (by disability, location or similar). One
accessibility issue is opening hours, and they become less or not
important, as digital library collections are available 24 hours a
day (Buckland, 1997, p. The Paper Library; Troll, 2002). However,
services need to be created with accessibility issues in mind, so
that users are not locked out (Schwartz, 2000). These include such
things as making sure that websites and programs are usable by
screen-readers for example. 
</p>
<p>In a digital
library, it becomes easier to collect usage information. This has a
number of potential side affects; for example, performance measures
become more accurate (Troll, 2002). It may also lead to more
customised services, depending on the individual. This maybe based on
“knowledge about individuals, their past behaviors and preferences,
and their connecting technologies” (Schwartz, 2000). Though, it is
also true that 'automated libraries' could offer a certain amount of
customisation due to the potential saving of past behaviours and
preferences. This may raise privacy concerns though. 
</p>
<p>Because the
collection is digital, it is not as restricted as a physical
collection; it could  potentially include digital items held by other
organisations. Related to this, digital material can, in the usual
case, be copied and shared. This makes archiving and preservation
easier (LOCKSS etc.) (though multiple libraries having multiple
physical copies of items also tended to preserve and archive items
(Troll, 2002), libraries cannot keep forever these items¸
particularly as they go out of date and/or stop being used). 
</p>
<p>Different types of
training (information literacy skills, etc.) are needed for both
users and staff (Schwartz, 2000). However, new tools can be used for
training, including interactive software. 
</p>
<p>Schwartz (2000) and Troll (2002)
discusses further differences, including relating to learning
facilitation, collaboration, training, the fact that the technology
may offer different ways of browsing the collection and preservation
issues. Chowdhury &amp; Chowdhury (1999) conducted a review of the
(then) literature, and would be a good place to start for more
issues. 
</p>
<p>It can be seen that
there are various and many potential and actual differences between
'traditional' physical libraries, and 'digital' virtual libraries.
However, these differences are not fundamental. They do not mean that
the the digital library is less or more of a library than the
physical library, or the hybrid library; any more than the
introduction of computerised catalogues meant that the new
'automated' libraries were less or more library than the libraries
using the older card catalogues. 
</p>
<p>Many of these differences are positive
benefits. However, careful consideration needs to be given to some
aspects. While digital documents are generally more flexible, DRM and
licensing could restrict usage and make them less preferable to
users. As well, DRM could restrict preservation efforts (as could
incompatible file formats, and obsolete media (such as floppy
disks)). Chowdhury (2010) discusses digital preservation in much more
detail. Accessibility is also a potential problem. For example, while
it is easy to make digital text documents usable by screen-reader
users, it does not always happen. As well, to use the digital
library, a user needs to be able to connect, which may require
unaffordable technology (particularly in developing regions).  
</p>
<h2>Conclusion</h2>
<p>The term 'digital
library' has been around for at least 24 years, and possibly longer,
and some of the ideas behind the term have been around for much
longer. While there is still disagreement about the meaning of the
term, this piece argues that, digital libraries are not fundamentally
different from 'traditional' libraries; either in the purposes of
existing, or in the services provided. The main differences come down
to how the services are provided, and how collections are stored and
managed. 
</p>
