	For those who don't know, my next CD is almost done, and I'm asking for your help to fund it through <a href="http://www.joshwoodward.com/kickstarter">a Kickstarter&nbsp;campaign</a>. The funding goal was met in&nbsp;<em>two </em><em>days</em>​. That's.. well, wow.</p>
<p>
	I've known for a long time that my listeners are awesome. But I also look at this as a victory for Creative Commons, and for independent music as a whole. Ever since I started releasing music almost 10 years ago, it's been completely free for the world to download. I'm a geek in my other life, and I come from an open source, free software background, so applying the same philosophy to music was natural.&nbsp;</p>
<p>
	But free music isn't free to make or distribute. Aside from the instruments, microphones, preamps, speakers, acoustic treatment, video cameras, interfaces, recording software, plugins and CD manufacturing, my website is spitting out over a terabyte (!) of data a month. So without the generosity&nbsp;of my listeners, none of this would be practical. And yet, despite the fact that you're free to grab my catalog, and listen to / use it for no cost, so many of you step up to the plate to help out not just with this Kickstarter, but over the years in many other ways.&nbsp;</p>
<p>
	People don't want to steal music, as a whole. They want to support the artists they believe in. And more and more, those people understand that buying a major label CD at Walmart is not supporting the artist on the cover in any meaningful way. I get almost 100% of what you're contribute to my Kickstarter campaign - you're very directly making my music possible. We're right here living the future of music, and it's beautiful.&nbsp;</p>
<p>
	My fundraising goal is met, but the $1,500 goal was only the bare minimum to make the CDs. There's a ton of stuff I'd still love to be able to do. Not the least of which is to get a new preamp - my Portico is dead, so I've been having to use the crummy built-in preamps for my interface to record. I'd also love to get a real Wurlitzer electric piano and a bunch of other instruments. Everything that you contribute from here on in goes toward making the music you get sound better, and allows me to focus more of my time on music.&nbsp;So please, <a href="http://www.joshwoodward.com/kickstarter">hop over to the Kickstarter campaign page</a> and consider kicking in if you're able.&nbsp;</p>
<p>
	Thank you, thank you, thank you for everything. I'm really moved.</p>
