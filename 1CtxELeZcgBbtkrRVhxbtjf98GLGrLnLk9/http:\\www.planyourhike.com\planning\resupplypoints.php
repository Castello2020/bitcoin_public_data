				<h2 id="pctrp"><a href="#">The Complete List of PCT Resupply Points</a></h2>




				<p>PlanYourHike.com has painstakingly researched 101 resupply points along the Pacific Crest Trail
				in an attempt to create one complete collection of all PCT resupply points.  As far as we know,
				no other online resource has anywhere near this many resupply points available to learn about. We hope this abundant resource helps you as you plan an awesome adventure on the PCT.  <a id="addBookmarkContainer"></a></p>

<p>
The numbers before the resupply point are read as follows.  The four digit number before the “+” indicates how far from Mexico (in trail miles) you'll have to walk before you get there.  The two digit number after the “+” indicates how far off the trail you must hike to get to the resupply point.  For example, if you see “1198+02 Sierra City PO”, that means that the Sierra City Post Office is a 1198 mile walk from Mexico and a 2 mile walk off the PCT.  Click the link to see more information.</p><p>
The resupply points marked with a "*" are the ones we recommend most.  We welcome updates/corrections and will always try to update this page as soon as we get new info.  That being said, <b>always call your resupply points and make sure that this info is accurate <u>before</u> you send any packages.</b>  This is particularly important because of the recent budget cuts to the post offices.  Make sure, when you call the post office, that your package is going to be held at the post office nearest to the PCT.  If you find any info that needs to be updated, please email us and let us know ASAP.  Thank you!</p>

