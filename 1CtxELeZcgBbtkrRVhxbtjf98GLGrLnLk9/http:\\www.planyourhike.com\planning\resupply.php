				<h2>Resupplying Strategies</h2>
				
				<p>Getting gear, food, fuel, and other supplies along the trail is a lot of fun.  There are many
					ways to do it.  You can have supplies sent to you. You can buy them along the way. You can have 
					people bring you your supplies on the trail. You can cache supplies in some places along the trail ahead of time.
					You could even live out of hiker boxes for some of the trail.  The only limit is your own creativity.</p><p>
					In this section we'll take a look at some of your options, talk about the pros and cons of each, and hopefully 
					give you enough information for you to figure out how you'll resupply on <i>your</i> hike.
				</p>
				
				<h2>Mailing Resupply Boxes</h2>
				<p>You can have resupply boxes sent to you along the trail.  There are dozens of post offices, hotels, stores, and lodges that 
will receive your package and hold it for you.  All post offices along the trail will hold packages for you - at no extra charge - for up to 30 days (then they 
return them).  Many hiker-friendly stores and lodges will also receive and hold packages for you for free, or for a small fee (from $2 to $20). 
</p><p>
You can check out <a href="./resupplypoints.php">PlanYourHike.com's complete list of PCT resupply points</a> to find the addresses, phone numbers, fees, and more for
<i>all</i> of the resupply points along the Pacific Crest Trail.</p><p>


</p>

<p><b>Pros:</b> Some small towns along the Pacific Crest Trail offer very few options for thru-hikers 
looking for lightweight pack food and reasonably priced gear.  Having food and gear sent to you at those places can save you a lot of money and unnecessary weight in your
 pack. If you have all of your supplies mailed to you then you don't need to run around gathering supplies when you get to town.  You can spend that time relaxing and hanging out.

</p><p><b>Cons:</b> Depending on what kind of mailing service you use, having your supplies sent to you can be expensive.  The cheapest and most reliable seems 
to be the U.S. Post Office's Flat-Rate Priority Mail service.  They charge you by the box <i>size</i> instead of the box <i>weight</i>.  In larger towns 
with lower prices and more food options, it is generally cheaper to buy your food in town than to have it sent to you.  Finally, you may not like the food you send to yourself. 
 One's appetite and the kinds of foods they crave can change quite a bit after a few weeks on the trail.  So be generous with the serving sizes and send yourself
 a variety of foods.  The same ol' meals and snacks can get old fast.</p>
						

	
				<h2>The Bounce Box</h2>
				<p>
The Bounce Box is a box of gear and supplies that you mail (or "bounce") from one resupply point to another.  When you get to your resupply point, you open your Bounce Box, 
remove any supplies from it that you'll need for the next segment, and put in any supplies you're carrying but won't need for the next segment.  Then 
you mail the box ahead to another resupply point further down the trail.  You can repeat this with the same box as many times as you want.</p><p>

<b>Pros:</b> The Bounce Box can be used to save you money.  For example, when you are in a bigger town with cheaper supplies, you can purchase extra supplies,
pack only the supplies you'll need for the next segment, and bounce the rest ahead to one of the smaller, more expensive trail towns.  Then when you get to the
 smaller town, you won't be scalped by their higher food and gear prices.</p><p>The bounce box can be a great solution for people who don't have a support 
person to send them supplies while they're on the trail, or for people that live so far away that sending supplies from home is just too expensive.  In fact, 
it is even possible to <! a href="./selfresupply.php">self-resupply along the entire trail</a> using Bounce Boxes and purchasing supplies in the larger, cheaper towns.</p>
<p><b>Cons:</b> Mailing the Bounce Box over and over again can be expensive.  Using USPS Priority Flat-Rate mail can reduce the cost, but it can still add up over several resupply points.
There is also a small risk of your Bounce Box being delayed in the mail.  So, if using Priority Mail, you may want to always bounce the box to a town at least 6 days down the trail.
				</p>

				<h2>Hiker Boxes</h2>
				<p>Most of the common resupply points along the trail have hiker boxes.  If you have any food or gear that you don't want, you put it in the hiker box.
Other hikers do the same, and everyone is free to take from the box as they please.  If you are traveling northbound behind the 'pack', you'll probably find these boxes loaded
 with all kinds of food and other supplies. (If you're hiking southbound, then as soon as you see you're first northbounder you'll know the hiker boxes are about to get outrageously awesome)!
Some hikers have hiked hundreds of miles eating nothing but what they find in hiker boxes.  It's not a reliable plan - but it happens.</p><p>
<b>Pros:</b> Food and supplies in hiker boxes are totally free.  You don't have to buy them, and you don't have to mail them.  Also, the food one finds in hiker boxes
is as diverse as the people who hike the trail.  There is always something you've never seen before.  If you don't like what was sent to you in your resupply box, you can always
swap some things out with stuff from the hiker box.</p>
<p><b>Cons:</b> Hiker Boxes are really hit-or-miss.  Sometimes they're full of awesome stuff, and sometimes there is nothing but junk.  Hiker boxes are also unreliable.
There may not be enough food there for you to make it to the next resupply point.  The store\lodge may have decided to just throw it all out, or not host a hiker box that year.
Unless you call ahead, you can't be sure.  Finally, you don't know the quality of the food in the boxes, who's touched it (or where their hands have been), or how old it is.
				</p>

<p>
A quick warning: Some hikers plan on resupplying out of Muir Ranch's Hiker boxes, without sending a package there (which costs $50).  But Muir Ranch's policy will only allow access to the hiker boxes for hikers that are having a resupply package held there!  They may be more lax on this late in the season (when soouthbounders get there), but don't count on it.
</p>
				<h2>Buying Supplies Along the Way</h2>
				<p>
There is always some kind of food that can be bought and packed out from each resupply point along the trail.  The options available at each location -
 as well as their costs - varies widely from place to place. <a href="./resupplypoints.php">PlanYourHike.com's complete list of PCT resupply points</a>
includes information for <i>all</i> towns along the trail - including what stores are available and how expensive or inexpensive they are.
</p><p><b>Pros:</b> Buying your supplies along the way allows you to adapt quickly to changes in your gear needs and food preferences.  The food you buy is 
fresh from the store, and hasn't been sitting in a resupply box for months.  In some towns, buying your supplies from the store is cheaper than having things sent 
from home.

</p><p><b>Cons:</b> Buying your supplies along they way can also be <i>more</i> expensive in some towns.  Also, many small towns don't have lightweight hiker food.
 You may get stuck having to carry heavy food, perhaps encased in glass jars or tin cans.  You may want to plan on buying food in the cheaper towns, and 
using a Bounce Box or some other method of resupplying in the more expensive towns.  Also, buying food along the way can be 
a real hassle.  You'll have to spend a lot of time and energy running around town buying supplies, packaging, and sending them when you could have spent that time relaxing and recuperating.

				</p>

				<h2>Caching Supplies Along the Trail</h2>
				<p>
You may cache supplies near the trail before you start your hike, perhaps marking them with GPS waypoints.  Then as you hike the trail, you can find your caches 
and get your supplies.</p><p>
<b>Pros:</b> For some hikers, caching supplies along the trail is appealing because it allows them to hike further without having to leave the wilderness.
  It is also convenient to have your supplies so close to the trail, while other resupply strategies require that you occasionally 
walk or hitchhike several miles off the trail to get to a town.
</p><p>
<b>Cons:</b> It is very difficult to cache supplies along the trail while respecting our Leave No Trace principles.  Whatever you cache your supplies in
 (a box, bucket, etc.) needs to be packed out.  That can make backcountry caches impractical.  Caching near a road crossing with trashcans nearby is more practical
 since one can just throw the cache box (or bucket) into the garbage can.</p><p>
Caching is also not as reliable as other resupply methods.  If your cache is not hidden very well, it will be found by backpackers, ATVers, hunters, etc.
When a cache is found, it is rarely left alone.  Even if you hide your cache well, animals may still smell it out, dig it up, and eat it.  This is bad for you
 <i>and</i> the animals.  Finally, it may take a lot of time and money to visit your cache sites and cache your supplies ahead of time.  

				</p>

				<h2>Having Someone Bring You Supplies</h2>
				<p>
You may know someone dedicated enough to drive to a town or a road-crossing to bring you supplies.
</p><p>
<b>Pros:</b> If you are lucky enough to have this kind of support, you may be able to do some slackpacking - giving all your gear (except your water 
bottle and map\GPS) to your support person, hiking all day without your pack, then meeting your support person at a road crossing and getting your gear back.  You won't have 
to leave the trail for anything, and you'll be able to make great time.
</p><p>
<b>Cons:</b> Aside from being more expensive than other resupply methods, this requires excellent communication skills between you and your support person.
 Miscommunication can result in missed rendezvous - a potentially frustrating experience.  Having some kind of long-range communication devices (long-range walkie-talkies perhaps),
 and GPS (one for the hiker and one the vehicle support - both using the same waypoints) may help in this regard.
				</p>

				<h2>Mixing and Matching Strategies</h2>
				<p>
				Nobody says you can only have one way of resupplying.  This is your hike - have fun with it!  You might have resupply boxes sent 
to all the small, expensive towns.  Then buy food at the bigger, cheaper towns.  You might Bounce Box some snow gear from the High Sierras to Washington.  
Then maybe you'll have your friend from Seattle bring some supplies to you at a road crossing in Washington.  Anything is possible!

				</p>
