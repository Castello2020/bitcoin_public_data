<center><div id="my_chart"></div></center>
<p><strong>Why donate money?</strong> Linux Mint is free of cost and doesn't generate any direct sort of income. It is funded by advertising, sponsoring and donations and although it is financially supported by its own community of users, its ambition is to compete with projects which are backed by large companies such as Microsoft, Apple, RedHat, Novell and Canonical.</p> 
<p><strong>How is the money used?</strong> The money is used to finance the needs of the distribution, to pay for hosting, to advertise on other websites, to purchase equipment and to fund the work of the people behind it.</p>
<p><strong>How much should you give?</strong> The donations we receive range from a single dollar to a couple of hundreds. The important thing for people is to give something, and for us it's to generate the income that will let us work and succeed on this project, month after month. If you want to help, and if you can do so, please make a donation to the project and encourage others to do so.</p> 
<p><strong>What is the long-term plan regarding the funding of Linux Mint?</strong> We believe Linux Mint can gather the momentum and the community necessary to fund itself entirely through donations, sponsoring and advertisement without the need to engage in commercial activities and without focusing our attention on anything else than improving the distribution itself.</p>

