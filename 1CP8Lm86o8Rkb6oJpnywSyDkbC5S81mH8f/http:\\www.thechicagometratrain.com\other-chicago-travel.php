<font size=4>Traveling farther? Our suggestions:</font>
<br>
<font size=2>
<br>
A lot of people ask us, can you help me get to Champaign, IL or Decatur, IL? Or they need to get to Springfield or St. Louis.
<br><br>
Others need to know something about the Chicago area airports and getting to other US cities and other international locations.
<br><br>
So without further ado, here's our recommendations on traveling farther than Metra allows:
<br><br>
<font size=4>By train</font>
<li> <a href="http://www.transitchicago.com/travel_information/maps/htsystemmap.aspx">CTA - The "L"</a> trains go mostly 24 hours, but local changes can take place so you need to check out what's going on in each section. The link is to the graphical online map (just start clicking).
<li> <a href="http://www.amtrak.com">Amtrak</a> trains go to almost any part of the US, including towns within Illinois like Champaign, Decatur, Springfield, Macomb, and Bloomington.
<br><br>
<font size=4>By air</font>
<br> The two airport options are:
<li>  O'Hare International (ORD) - <a href="http://www.flychicago.com/OHare/OhareHomepage.shtm">official site</a>, <a href="http://en.wikipedia.org/wiki/O%27Hare_International_Airport">Wikipedia article</a>
<li> Midway (MDW) - <a href="http://flychicago.com/midway/MidwayHomepage.shtm">official site</a>, <a href="http://en.wikipedia.org/wiki/Chicago_Midway_Airport">Wikipedia article</a>
<br>
<br>
<li> We strongly recommend <a href="http://www.southwest.com">Southwest Airlines</a> for low-cost fares within the US. Only flies out of Midway.
<li> For good deals anywhere else, be sure to always check <a href="http://www.orbitz.com">Orbitz</a>.

<br><br>
<font size=4>By taxi</font>
<br> <a href="http://www.americantaxi.com/">American Taxi</a> is highly reccommended for travel throughout the Chicago area and to and from airports, keep this number: 1-847-255-9600



