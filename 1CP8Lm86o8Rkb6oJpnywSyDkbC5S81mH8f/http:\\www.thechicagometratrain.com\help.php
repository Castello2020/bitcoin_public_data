<br><br>
<b>What is the difference between "incoming" and "outgoing" for the train schedules?</b>
<br>
These terms refer to the direction from the city of Chicago trains are traveling. Incoming means the train is going toward Chicago. Outgoing means its going away from Chicago.
<br><br>
<b>How do you read the schedules on the Metra site?</b>
<br>
All times refer to when the train is supposed to leave that station. When a station has dashes "--" in its row, that means the train does not make a stop there.
<br><br>
