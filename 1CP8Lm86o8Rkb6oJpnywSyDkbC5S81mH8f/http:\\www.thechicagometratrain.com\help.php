<b>I ride Metra often as a commuter or occassionaly as a traveller, how can I help in the community effort to update train statuses on this web site?</b>
<br>
You can contribute to our train line updates with your cell phone using text messages. You will need a Twitter account and need to be familiar with how Twitter (it's easy).
<br><br>
All you need to do is get your Twitter account synced to your cell phone number. Then send a text message to Twitter, the number is 40404. Then your message text should look something like this:
<br><br>
<blockquote>
