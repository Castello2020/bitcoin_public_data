<h2 class="main">Download and Instructions</h2>

<p class="main">If you only want to show your user stats, you only need to dowload the <a href="../downloads/index.php?download=BOINC.widget">user version</a>. If you would like to show your team stats <u>as well</u>, then you also need to download the  <a href="../downloads/index.php?download=BOINCTeam.widget">team version</a>.</p>

<p class="main"><b>Notes:</b></p>

<ul class="main">
<li class="main">Both widgets have the same features and have the same requirements.
<li class="main">You must have at least version 4.0 of <a href="http://widgets.yahoo.com">Yahoo! Widgets</a> installed to use these widgets!
<li class="main">The widgets need an Internet connection in order to operate - it does not have to be an "always-on" connection, but you must be connected to the Internet when the applet is first loaded.
</ul>

<p class="download"><a href="../downloads/index.php?download=BOINC.widget">USER VERSION</a> <a href="../downloads/index.php?download=BOINCTeam.widget">TEAM VERSION</a></p>

<h2 class="main">Installing/Upgrading</h2>

<p class="main"><b>Note:</b> Make sure you have to download and installed <a href="http://widgets.yahoo.com">Yahoo! Widgets</a> before proceeding.</p>

<p class="main"><b>1:</b> If an instance of the widget is already running, close it first. (Right click -&gt; Close widget).</p>

<p class="main"><b>2:</b> Left click one of the download links above and choose "Save" when prompted, and save to your "My Widgets" folder.</p>

<p class="main"><b>3:</b> To start the widget, simply double click the file in your My Widgets folder or open it via <a href="http://widgets.yahoo.com">Yahoo! Widgets</a>.</p>

<p class="main"><b>4:</b> If upgrading, you will be warned that the widget has been modified since you last used it. Click "ok".</p>

<p class="main">The widget needs to know what your user ID is. Simply right click the widget and select "Widget Preferences" and enter your user ID. If you do not have a user ID, simply <a href="register.php">register for free</a>.</p>

<p class="main">Currently the widget will contact <a href="http://boinc.mundayweb.com">boinc.mundayweb.com</a> for your stats every 8 hours.</p>

<p class="main"><b>Please do not modify the widget to increase the frequency of the updates as this will put an increased strain on my server and is not necessary as your stats are only updated once a day.</b></p>

