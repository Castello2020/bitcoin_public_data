<h2 class="main">Features</h2>

<p class="main">The widget offers the following features:</p>

<ul class="main">
<li class="main">Downloads your stats periodically from MundayWeb
<li class="main">Allows you to configure the colour scheme, including background image choice
<li class="main">Allows you to choose the background colour (where applicable)
<li class="main">Provides an update feature to alert you of new widget versions - For new versions, you can simply overwrite your previous copy with the new one.
<li class="main">Allows you to choose how your stats are displayed
<li class="main">Automatically resizes to accommodate the number of projects you have
<li class="main">Allows you to choose both the widget's opacity and the background image's opacity
<li class="main">Allows you to limit the total number of projects shown
<li class="main">Works with the widget dock
</ul>

