        <h1>How To Make A Website In 8 Steps</h1>
        <ol>
           <li>Think of a topic that you know lots about.  Make	sure	that it is something that you will  enjoy writing about.</li>
           <li>Using your favorite <a href="http://www.openoffice.org/">word processor</a> write 15 pages of content for the website. </li>
           <li>Go to <a href="http://www.worldforwebsites.com/">WorldForWebsites</a> (or some other place) and get a domain name and an Economy hosting plan.</li>
