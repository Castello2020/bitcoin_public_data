 (or <a href="http://www.gimp.org/">free image editing software</a>) and create a logo for your website.&nbsp; If you do not want to mess around creating your own logo you can <a href="http://99designs.com/">hire somebody to design a logo</a> for you.</li>
           <li>Get website design software like <a href="http://www.amazon.com/gp/product/B001EUDIZE?ie=UTF8&tag=howthiswebsim-20&linkCode=as2&camp=1789&creative=9325&creativeASIN=B001EUDIZE" rel="nofollow">Adobe Dreamweaver CS4</a><img src="http://www.assoc-amazon.com/e/ir?t=howthiswebsim-20&l=as2&o=1&a=B001EUDIZE" width="1" height="1" border="0" alt="" style="border:none !important; margin:0px !important;" />
   (or  <a href="http://www.jdoqocy.com/click-4511578-10366838">the less expensive CoffeeCup</a>) and create your website.&nbsp; Do this <a href="http://www.w3.org/Style/Examples/011/firstcss">HTML and CSS tutorial</a> if you need help creating your website.&nbsp; If you do not want to mess around with all the technical details of creating a website  try <a href="https://www.securepaynet.net/hosting/website-builder.aspx?ci=1805&prog_id=worldforwebsites&isc=in_body" rel="nofollow">WorldForWebsites Website Builder</a>.</li>
           <li>Get an account with <a href="http://www.google.com/adsense/">Google AdSense</a>. Put Google advertisements on your website.</li>
           <li>Publish your website.</li>
           <li>Keep updating your website with new content.</li>
        </ol>
        <p>For this website steps 1-6 took <a href="how-make-my-own-website.php">12 days</a> (between December 7, 2008 and December 31, 2008) and <a href="../data/data-hours-worked-on-website.php">47 hours</a>.  For more details about this process please see my blog entry - <a href="../blog/2009/01/20/time-to-build-a-website/">Time to Build a Website</a>.  </p>
        <p>Make sure that before you begin creating your website and dreaming of instant riches have a look at <a href="how-make-my-own-website.php">this website's development timeline</a>.&nbsp; This should provide you with&nbsp;a glimpse&nbsp;into your development process - hopefully you can avoid the mistakes  I made.</p>
        <p>Do you want to know your near future as a webmaster?&nbsp; Trust me - there are no mansions, no sport cars, no days on the beach with a martini in one hand and a cigar in the other.&nbsp; Want to know where you will  be with your website in the <a href="month_one_it_works.php">first month</a>, in <a href="month-three-page-rank-increase.php">3 months</a>, in&nbsp;<a href="month_six_google_sandbox.php">6 months</a>, in a <a href="creating-website-content-guest-post.php">year and a half</a>?</p>
        <p>If you are not sure how to proceed with promoting your website and generating traffic  then have a look at the <a href="how-to-get-more-website-traffic.php">how to get more traffic to your website</a>.&nbsp; This should get you going - from then on you will be able to tweak to your own needs.</p>
        <p>Do not think about it too much - start  a website today, think about why and how later.&nbsp; The worse thing that can happen is you learn something  and make a few dollars.&nbsp; And it's fun.&nbsp; You will meet  people, you will do lots of writing and reading, you will go to bed with anticipation, you will wake up to surprises.&nbsp; </p>
        <p>Even though I bitch and moan on this site about how much hard work it is to make money, I do not regret it for a minute.&nbsp; I might quit tomorrow but I will never forget this adventure.</p>
        <p>It's  not  easy to make money with a website but it is  fun&nbsp;trying.</p>
