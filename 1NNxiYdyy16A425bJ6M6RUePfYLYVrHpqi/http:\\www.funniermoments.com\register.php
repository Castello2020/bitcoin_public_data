<script type="text/javascript">
$(document).ready(function () {
	$('#appendedInputButton').typeWatch({
			callback: function() {
					var a = $('#appendedInputButton').val();
					
					$.ajax({
						type: "POST",
			            url: MELODYURL2 + "/ajax_search.php",
			            data: {
							"queryString": a
			            },
			            dataType: "html",
			            success: function(b){
							if (b.length > 0) {
			                    $("#suggestions").show();
			                } else {
								$("#suggestions").hide();
							}
							$("#autoSuggestionsList").html(b);		
						}
					});
				},
		    	wait: 400,
		    	highlight: true,
		    	captureLength: 3
		});
	$('#appendedInputButton').on('keydown',function(event){
			switch (event.which) {
                case 38:
                    if ($('#autoSuggestionsList>li.active').length > 0) {
                        $('#autoSuggestionsList>li.active').removeClass('active').prev().addClass('active');
                    }
                    else {
                        $('#autoSuggestionsList>li:last').addClass('active');
                    }
                    event.preventDefault();
                    break;
                case 40:
                    if ($('#autoSuggestionsList>li.active').length > 0) {
                        $('#autoSuggestionsList>li.active').removeClass('active').next().addClass('active');
                    }
                    else {
                        $('#autoSuggestionsList>li:first').addClass('active');
                    }
                    event.preventDefault();
                    break;
                case 13:
                    if ($('#autoSuggestionsList>li.active').length > 0) {
                        window.location = $('#autoSuggestionsList>li.active a').attr('href');
                    }
                    event.preventDefault();
                    break;
            }
		});
});
</script>
