          <p><b>Stirling</b> is the correct spelling, by the way. I
          used to think they were spelled "sterling engines." (I
          could probably get away with calling my engines sterling
          engines because I lived near the town of Sterling,
          Alaska.) Nevertheless, <i>Stirling</i> engines are named
          after their inventor, Robert Stirling.</p>

          <p>Some information sources out there confuse ordinary
          heat engines with stirling engines. Stirling engines,
          unlike other heat engines, use a <a href=
          "http://www.ent.ohiou.edu/%7Eurieli/stirling/simple/regen_simple.html">
          regenerator</a>, which recycles internalenergy (heat)
          many times. Feel free to browse the illustrations, but
          the best way to learn how stirling engines work is to
          build one!<br></p>

