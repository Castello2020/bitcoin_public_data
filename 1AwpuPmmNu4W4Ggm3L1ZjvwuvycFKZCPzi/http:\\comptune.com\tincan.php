          <p><b>What I like about Stirling engines:</b></p>

          <blockquote>
            <p><b>Double-action piston:</b> It is a true one-stroke
            engine, since the piston both pushes and pulls,
            delivering smooth power on every stroke. A car engine,
            on the other hand, is a 4-stroke (or, in rare cases, a
            2-stroke) engine. It only delivers rough, explosive
            power on every 4th stroke. As a result, car engines
            wear out much quicker and produce more noise.</p>

            <p><b>Simple design:</b> There are no spark plugs,
            coils, wire, points, injectors, valves, camshafts, oil
            sump, pumps, filters, carburetor, mufflers or timing
            belts/gears to wear out.</p>

            <p><b>Efficiency:</b> With modern designs and materials
            up to the maximum theoretical thermodynamic efficiency
            is possible because of the way heat is recycled many
            times inside the engine instead of used once and thrown
            away as in conventional engines.</p>

            <p><b>They will run on almost any fuel:</b> Power may
            be the heat of a candle or concentrated sunlight. Some
            carefully-built models can even run on the heat from
            warm hands. They will also run on chemical processes
            that produce cold instead of heat. I was able to run
            the HK1 at great speed on liquid nitrogen until the oil
            became hard and brittle (next time I'll use
            graphite).</p>

            <p><b>Quiet running:</b> At maximum power all that can
            be heard is slight whirring or clanking and only then
            if the bearings are loose. I've witnessed engines that
            run totally silent and are even compensated for
            vibration.</p>

            <p><b>Safety:</b> Stirling engines are probably the
            safest engine to work with. They can't explode like
            steam engines and the models on this page don't really
            put out enough power to injur anyone. I suppose sunburn
            is possibble (when setting up a solar engine) and
            solering irons are hot. Use adult supervision!</p>

            <p><b>Innovative potential:</b> Old-style stirling
            engines did not perform nearly as well as their
            internal combustion cousins. Their low power-to-weight
            and power-to-size ratios made them unreasonable
            candidates for automotive power plants. There were a
            few attempts at powering automobiles with stirling
            engines but consumers wanted more 'pep'. Stirling
            engines can often take several minutes to warm up &amp;
            deliver useful power. With space-age materials,
            however, the stirling engine is freed from these
            limitations. <a href=
            "http://www.google.com/search?hl=en&amp;lr=&amp;safe=off&amp;q=stirling+hev">
            Stirling HEV's</a> may be the transportation of the
            future..</p>
