Secondo la leggenda il Buddha nel presentimento della sua fine terrena,
chiam&ograve; a raccolta tutti gli animali della terra, ma di questi solo 12
andarono ad offrire il loro saluto. Come premio dunque per la loro
fedelt&agrave; il Buddha decise di chiamare ogni anno del ciclo lunare con il
nome di ciascuno dei 12 animali accorsi. Il topo, furbo e veloce di
natura, arriv&ograve; per primo. Il diligente bue arriv&ograve; secondo, seguito
dall'intrepida tigre e dal pacifico coniglio. Il drago arriv&ograve; quinto
seguito subito dal suo fratello minore, ovvero il serpente. L'atletico
cavallo fu settimo e l'elegante pecora ottava, subito dopo arriv&ograve;
l'astuta scimmia, e poi ancora il coloratissimo gallo, il fedele cane
per poi finire con il fortunato maiale che arriv&ograve; appena in tempo per
salutare anch'egli il Buddha.<br><br>

Sempre la leggenda spiega come mai il piccolo e furbo topo riusc&igrave; a
battere il grande e onesto bue, arrampicandosi sul suo dorso evit&ograve; di
percorrere la strada per arrivare all'Illuminato e giunto sul luogo
salt&ograve; il bue e salut&ograve; il Buddha.<br><br>

Da notare che nel 2013 comincer&agrave; l'anno 2557 dell'era buddista, infatti
tanti anni sono passati da quella notte del 545 a.C. in cui il principe
Siddhatta pervenne al supremo risveglio e divenne un Buddha. Da quella
data si cominciano a contare gli anni della &laquo;dispensazione&raquo; (<i>sasana</i>),
ossia dell'era in cui gli uomini hanno la possibilit&agrave; di por fine al
dolore grazie agli insegnamenti del nobile asceta della stirpe dei
Sakya.<br><br>

Pu&ograve; interessare evidenziare come, a differenza dei cristiani, che
contano gli anni dalla nascita di Ges&ugrave;, i buddisti li contino dal
risveglio, ossia dal conseguimento della buddit&agrave;, perch&eacute; solo da quel
momento in poi si rese disponibile l'insegnamento di un Buddha: infatti
prima di quel giorno Siddhatta era solo un <i>bodhisatta</i>, ossia un
Buddha in potenza, ma non ancora in atto.<br><br>

Si dice siano esistiti molti Buddha e che il principe Siddhatta della
stirpe dei Sakya sia solo l'ultimo di una lunga serie e l'unico di cui
siano arrivati a noi gli insegnamenti.<br><br>

Una interessante nota culturale &egrave; che i cinesi hanno una concezione
ciclica del tempo mentre noi occidentali abbiamo una visione lineare
del tempo, moltissime societ&agrave; antiche hanno questo concetto del tempo
come descritto da questo <a href="http://nextloop.wordpress.com/2008/02/15/tempo-lineare-tempo-ciclico/">erudito
articolo</a> (<small>sito protetto, cercher&ograve; una fonte alternativa</small>).<br><br>

I nomi degli animali sono nel calendario cinese i seguenti:<br>

zi (topo), chou (bue), yin (tigre), mao (coniglio), chen (drago), si
(serpente), wu (cavallo), wei (pecora), shen (scimmia), you (gallo), xu
(cane), hai (maiale). (elenco tratto da Wikipedia)<br><br>

Se siete interessati a sapere di che <a href="http://www.tuttocina.it/tuttocina/zodiaco/zodiaco.htm">segno
zodiacale cinese</a> siete allora questa tabella fa per voi.<br><br>

Il carattere&nbsp; delle persone che nascono&nbsp; in un certo anno del
calendario cinese &egrave; determinato dall'animale di quell'anno e modificato
dall'elemento, nel corso della sua vita&nbsp; incontrer&agrave; anni dominati
da altri animali e da altri elementi che influenzeranno la sua condotta.<br><br><a href="http://italian.cri.cn/Panorama/Curiosita%27/articoli/segni%20zodiacali.htm">Radio
Cina Internazionale</a> riporta il carattere di ogni uno dei 12 animali.<br><br>

Per quanto riguarda i nati sotto il segno del drago, <a href="http://it.wikipedia.org/wiki/Drago_%28zodiaco_cinese%29">Wikipedia</a>
(fate una piccola donazione se potete) ne parla lungamente: &laquo;In Cina,
il Drago &egrave; il segno dell'Imperatore cinese o l'elemento maschile Yang.
Il Drago &egrave; il simbolo del potere e della ricchezza.&raquo;<br><br>

Per quanto riguarda i nati sotto il segno del serpente, <a href="http://it.wikipedia.org/wiki/Serpente_%28zodiaco_cinese%29">Wikipedia</a>
(fate una donazione se potete) ne elenca lungamente e dettagliatamente
le tante abilit&agrave;.<br><br>

I nati dell'anno cinese 2011 sono dello Xin Mao. Quelli dell'anno
cinese 2012 sono dello Ren Chen. Quello del 2013 dello Gui Si. Quello
del 2014 dello Jia Wu.<br><br>

Da notare che compare l'accoppiata segno animale (il secondo) e
l'elemento essenziale (il primo).<br><br>

Per comprendere il calendario e per predire come si chiamer&agrave; il
prossimo anno &egrave; sufficiente seguire la regola in cui:<br><ul><li>l'anno prossimo avr&agrave; il prossimo elemento nella sequenza legno,
fuoco, terra, metallo, acqua, dove ogni elemento si ripete due volte,
la prima volta sar&agrave; maschile e la seconda femminile</li>
  <li>l'anno prossimo avr&agrave; il segno animale seguente a quello di
quest'anno:&nbsp; zi (topo), chou (bue), yin (tigre), mao (coniglio),
chen (drago), si (serpente), wu (cavallo), wei (pecora), shen
(scimmia), you (gallo), xu (cane), hai (maiale).</li>
</ul>

Per esempio se quest'anno &egrave; legno maschile e topo, il prossimo sar&agrave;
legno femminile e bue, quello che seguir&agrave; sar&agrave; fuoco maschile e tigre,
ecc, ecc. L'ultimo anno del ciclo &egrave; acqua femminile e maiale a cui
ovviamente segue il primo del nuovo ciclo e cio&egrave; legno maschile e topo.<br><br>

Un ciclo &egrave; iniziato nel 1924&nbsp; ed &egrave; finito nel 1983, il ciclo in
cui siamo adesso &egrave; ovviamente iniziato nel 1984 e terminer&agrave; nel 2043:
siamo quindi a circa met&agrave; ciclo.<br><br>

Segue una piccola nota matematica che gli umanisti possono
tranquillamente saltare.<br><br>

Gli elementi sono 5 per 2 in quanto sono sia maschile che femminile,
quindi sono 10. I segni animali sono 12. Entrambi vengono ripetuti fino
a quando non si arriva all'ultimo elemento che coincida con l'ultimo
segno: quando questo avviene?<br><br>

Avviene quando 10*<i>n</i>=12*<i>m</i> con <i>n</i> e <i>m</i> pi&ugrave;
piccoli possibile cio&egrave; il minimo comune multiplo (mcm) &egrave; l'ampiezza del
ciclo del calendario cinese ovvero mcm(10,12)=60. Quindi <i>n</i>=mcm(10,12)/10
e <i>m</i>=mcm(10,12)/12 o <a href="http://it.wikipedia.org/wiki/Minimo_comune_multiplo">anche</a> <i>n</i>=12/MCD(10,12)
e <i>m</i>=10/MCD(10,12), dove MCD &egrave; il massimo comune divisore ovvero
2.<br><br>

Si evince che gli elementi si ripetono <i>n</i>=6 volte nell'intero
ciclo mentre gli animali si ripetono <i>m</i>=5 volte.<br><br>

La met&agrave; del ciclo si ha nel transito da acqua femminile a legno
maschile e degli associati serpente e cavallo, questo perch&eacute; la met&agrave;
cade dopo 3 ripetizioni degli elementi e dopo 2.5 ripetizioni degli
animali.<br><br>

Una piccola nota riguarda il fatto che sia sessualizzando gli elementi
(maschie, femmine) cio&egrave; raddoppiandone il numero, sia non facendolo, si
ha che il ciclo rimane di 60 anni, infatti mcm(5,12)=mcm(10,12)=60.<br><br><b>Il ciclo sessantennale dello zodiaco cinese<br><br></b>Animali: zi (topo), chou (bue), yin (tigre), mao (coniglio), chen
(drago), si (serpente), wu (cavallo), wei (pecora), shen (scimmia), you
