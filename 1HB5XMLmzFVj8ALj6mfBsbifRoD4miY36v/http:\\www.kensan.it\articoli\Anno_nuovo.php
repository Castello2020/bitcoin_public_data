Il giorno 10 febbraio 2013 &egrave; iniziato l'anno cinese (e vietnamita) del
Serpente sotto l'elemento
acqua femminile (serpente d'acqua). L&rsquo;anno della Serpente terminer&agrave; il
30 gennaio 2014, il 31 inizier&agrave; quello del Cavallo .<br><br>

L'anno nuovo 2012 &egrave; l'anno del Drago sotto l'elemento acqua maschile.<br>

L'anno nuovo 2013 &egrave; l'anno del Serpente sotto l'elemento acqua
femminile.<br>

L'anno nuovo 2014 &egrave; l'anno del Cavallo sotto l'elemento legno maschile<br><br>

Un elemento interessante &egrave; che il capodanno cinese del 30 gennaio 2014
coincider&agrave; con la via di mezzo del ciclo sessantennale dello zodiaco
cinese, una specie di mezzogiorno del ciclo che avverr&agrave; alla mezzanotte
del 30.<br><br>

Il calendario lunare cinese secondo la tradizione risale al 2637 a.C.
quando l&rsquo; imperatore Huang Ti introdusse il primo ciclo del suo
Zodiaco. L&rsquo;unit&agrave; di misura del suo sistema &egrave; un ciclo composto da 60
anni, suddiviso in cinque cicli di 12 anni.<br><br>

Durante il ciclo ognuno dei segni animali (12) &egrave; combinato con gli
elementi essenziali e primitivi (5), formando il ciclo di 60 anni
(12x5):<br><blockquote>
