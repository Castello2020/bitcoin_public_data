	<h1 id="checksums-drag-and-drop-wee-brother">simple checksum</h1>
	<h2 class="sub">checksum's little brother!</h2>

	<img src="img/simple_checksum.png" class="right-img-title" id="simple-checksum" alt="simple checksum's cute wee window" title="powerful MD5, SHA1 and BLAKE2 checksum creation in a handy, pocket-sized, drag-and-drop app." />

	<p>Wherever <a href="http://corz.org/windows/software/checksum/" title="Get checksum, will ya! Hashes for the masses!" id="link-site-checksum"><strong>checksum</strong></a> goes, his wee brother usually tags along. He's called <strong>simple checksum</strong>, an ultra-low profile desktop application, designed for ad-hoc drag-and-drop hashing and compare tasks. He's the good-looks to checksum's muscle, which he borrows. He really is a mere strip of a lad!..

	<div class="space-tiny"></div>

	<h2>Simple..</h2>
	<p>Yes, but he's smarter than he looks, and not only supports drag-and-drop file hashing, but on-demand string hashing, as well as hash-perfect <a href="#file-comparison" title="compare files, with bit-perfect hashing, on a simple drag &amp; drop" id="link-page-file-compare-hashes">file and folder comparison</a>. You can also drag and drop files and folders directly onto his program icon (or a shortcut to it, maybe on your desktop), as well as supply file names on the command-line and/or hashes in the clipboard, to launch immediately into a checksum calculation or comparison job, done. Simple, sure, but clever, too!

	<div class="clear-tiny"></div>
	<h2>Fast..</h2>

	<p>Uses the same ultra-fast 32 or 64 bit hashing engine as his big brother, <a href="http://corz.org/windows/software/checksum/" title="Best checksum application in the world? You decide!" id="link-checksum">checksum</a>. Enough said.

	<h2>Yet, fully featured..
	<img src="img/simple_checksum_sha1.png" class="left-img" id="simple-checksum-sha1-mode" alt="simple checksum doing SHA-1" title="simple checksum doing SHA-1" /></h2>

	<p>simple checksum supports <a href="md5-sha1-blake2-algorithms.php" title="What is MD5? What is SHA1? What is BLAKE2? Answers here" id="link-algorithms-explained">MD5, SHA1 and BLAKE2 hashing algorithms</a> and is completely FREE (it comes with <a href="http://corz.org/windows/software/checksum/" title="Best checksum application in the world? More and more are saying 'YES!'" id="link-2-checksum">checksum</a>). So feel free to throw a shortcut somewhere handy (maybe in your SendTo menu), always ready for instant hashes and file/folder comparison jobs.

	<div class="clear-tiny"></div>


	<p>simple checksum is designed to sit out-of-the-way somewhere, and supports transparency and "always on top" bit-setting for extended desktop usage, enabling you to do visual comparisons of online checksums, even with your web browser open full screen.

	<img src="img/simple_checksum_transparent_window.png" class="right-img-title" id="simple-checksum-transparent" alt="simple checksum doing transparency" title="transparency and 'always-on-top' enable you to keep simple checksum open in the corner of your desktop for extended periods of time." />

	<p>You can show/hide simple checksum by simply clicking its system tray icon. Everything has ToolTip help, even the system tray icon.


	<p>Easily switch between MD5, SHA1 and BLAKE2 hashing with HotKeys or via simple checksum's application menu - simple checksum will recalculate the file's hash using the new algorithm, without the need for a second drag-and-drop.


	<p><img src="img/simple-checksum-switch-algorithm-menu.png"  id="simple-checksum-switch-algorithm-menu" alt="simple checksum's application menu - algorithm sub-menu selected" title="set things temporarily, or save them for the future... it's the prefs!" style="margin: 5px;" />

	<p>Launch simple checksum with a file (via drag &amp; drop, SendTo, etc.), and get an instant MD5, SHA1 or BLAKE2 hash, ready to view or copy into your clipboard. Launch with a hash in the clipboard, perhaps with drag-and-drop, and simple checksum performs an instant hash compare..


	<p><img src="img/simple_checksum_clipboard_compare.png" class="right-img" id="simple-checksum-compare-mode" alt="simple checksum doing clipboard commparison" title="simple checksum clipboard hash compare" />


	<p>simple checksum's <code>Auto..</code> menu enables you to automatically send your hashes to either your Windows&reg; ClipBoard, or directly to a fresh <code>.hash</code> file, as part of one simple drag and drop. Switch algorithm (<code>Alt+M</code>, <code>Alt+S</code> or <code>Alt+B</code>) and with <code>AutoSave</code> enabled, you've got yourself a MD5+SHA1+BLAKE2 <code>MultiHash&trade;</code> from a single drag and drop!

	<p>Drop <em>Two</em> files, and simple checksum transforms into a simple, highly efficient file comparison utility&hellip;



	<div class="clear-tiny"></div>

	<h2 id="file-comparison">File Compare..</h2>

	<p>Drop <em>Two</em> files onto simple checksum; either the program itself (or a shortcut to it), or directly onto its input, and simple checksum will compare them, using your currently selected hashing algorithm. If the files are identical, you get "Files MATCH!", and if not, "Files DO NOT MATCH!", simple as that.


	<p class="top-tip">If required, you can get to the actual hashes by hitting Alt-C. If the hashes were different, hitting Alt-C a second time will get you the second hash. From then on, Alt-C toggles between the two hashes.


	<p class="top-tip">When comparing files, if <code>AutoCopy</code> is enabled, your clipboard will contain a mini-report, with the two file names, their individual hashes, and right at the top, the status of the file comparison; match or not.


	<div class="clear-tiny"></div>

	<h2 id="folder-comparison">Folder Compare..</h2>

	<p>Drop two <em>Folders</em> onto simple checksum; either the program itself (or a shortcut to it), or directly onto its input, and simple checksum will automate the process of hashing both folders in turn (using checksum), then compare the two resultant <code>.hash</code> files (by hashing them, of course!). If they are identical, you get..

	<p><code>"Folders MATCH!"</code><img src="img/simple_checksum_folder_compare.png" class="right-img-title" id="simple-checksum-folder-compare" alt="simple checksum performed a folder comparison" title="simple checksum uses checksum as its back-end for hash-perfect folder comparisons." />


	<p>If they are NOT identical, you get..

	<p><code>"Folders DO NOT MATCH!"</code>

	<p>Simple as that!



	<div class="clear-tiny"></div>

	<h2 id="web-hash-comparison">Web Hash Compare..</h2>

	<p>If you send a file to simple checksum (via Drag &amp; Drop, SendTo menu, or by dropping a file on its input) and there is a hash in your clipboard, simple checksum will immediately compare it to the dropped file and report whether or not they match (rather than report the hash string - if you need that, IT'S IN YOUR CLIPBOARD!)

	<p>This makes it a no-brainer to compare the hashes of download files with the hashes published on web sites, simply copy the hash from the page (select it, hit Ctrl+C) and then drop the downloaded file into simple checksum. TADA!


	<p>The clipboard compare functionality also means that you can compare the hashes of two files that are not physically next to each other; with Auto-Copy enabled, drag the first file into simple checksum - its hash is placed in the clipboard. When you drag-and-drop the second file, simple checksum will compare its hash to the one in the clipboard.


	<p class="top-tip">simple checksum will automatically switch between MD5, SHA1 and BLAKE2 hashing depending on the type of hash in your system clipboard.


	<p class="top-tip">If you have Auto-copy-to-clipboard enabled, it will deactivate when comparing hashes with the clipboard, your original hash will still be there after the operation.


