The crucial caveat is that even <em>thinking </em>in terms of "good" and "bad" negates your ability to understand the true workings of Karma and its function in your spiritual evolution. I personally use the word "good" as a technical term, meaning "Good for You", same story for "Bad". In the technical sense, Karma is always "Good".<br />
<br />
When these polar concepts are enlarged into the global scope, that is "Mankind", we call them "Right" and "Wrong". But before you can really get a handle on Right and Wrong, you have to get past all this "good and bad".<br />
