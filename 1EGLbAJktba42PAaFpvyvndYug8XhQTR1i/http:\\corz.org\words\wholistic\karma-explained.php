Both "events" are exactly what the neighbours needed at their stage of evolution, of course. The two neighbours appear to get exactly the same "event", yet clearly it is very different, <em>to them.</em> And perhaps twin two has a thing for Llamas, and their scenario is unfolding in altogether different directions! Two people can look at a piece of art, a picture; one sees nothing, the other's life is changed. Same picture. <br />
