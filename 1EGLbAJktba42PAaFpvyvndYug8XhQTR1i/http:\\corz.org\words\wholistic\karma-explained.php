		<h3>preamble..</h3>
		Initially I had no idea how or why I got myself invited into this "******* Magick Group", then I saw the request for essays on Karma. Hmm.. interesting, was my thought. While I haven't had any spare time this last week, today I managed to catch up on all those digested emails. *phew* Anyway, now I <strong>completely</strong> understand why I was invited!<br />
		<br />

		I will now explain Karma Yoga..<br />
		<br />

		<h3>Karma Yoga</h3>
		This has been an area of intense study and experiment for me, not so much in the traditional scholarly sense, those methods are rarely used here, but in the personal sense, as if understanding of the Laws of Karma and its mechanics were somehow vital to me, fundamental to my existance. That is also a general effect of this time we live in.<br />
		<br />

		Like everyone bound to earth, my knowledge is limited, filtered, yet I have discovered many things which may be useful to your understanding. As much as I am allowed to share, I will expand some of these discoveries. I say "discoveries" because there is nothing very new to learn here, it was all worked out long ago. We may have been mapping out this territory, attempting an understanding of it for as long as man could consider such things, but the essential truth remains unchanged. Only its presentation differs, and in that process, an evolution of our understanding of Karmic Law is taking place. You <strong>can</strong> know more than your forefathers did. That is the way.<br />
		<br />

		What I present here is no more than a brief sketch, foods for thoughts, some juicy seeds and a couple of tricks too. Read some bits slowly. Karma is, well..<br />
		<br />

		The common conception is a nonsense, obviously, but a clever one, and surely does provide a little of the essence of Karma Yoga to the popular tastes; that the laws of cause and effect also work outside the realm of the purely mechanical, and most folks are content to believe that if they do "bad" stuff, then they will attract bad events. They also believe that doing good stuff will be rewarding, bring "good" events, stuff. People really do (want to) believe that they are punished by Karma. Like Karma Cares!<br />
		<br />

		This is all very childlike, of course, and if these people refuse to look further into the issue, is that our concern? Perhaps they <em>might</em> do that, if someone would only explain it to them in a way that makes sense, someone who knew what they were talking about. Truth is, most people <em>need</em> to work with the concept at this childlike level for a very long time before even the slightest glimmer of real understanding comes. But it does come. In the meantime they try to behave.<br />
		<br />

		For the interested, the usual texts are littered with references to Karma, some very precise and enlightening, others much less so. So much misleading garbage on the subject exists that "writing about Karma" is very nearly a pointless task, literally. But the laws of Karma are well expounded in the work of every era and at every time, even today. Our current artistic output is literally creaking with examples. I decide here and now to make no references whatsoever! This piece will function as well as any. So, what is Karma?<br />
		<br />

		<h4>Karma is a lie.</h4>
		You'd better believe it!<br />
		<br />

		<h4>Karma is an energy.</h4>
		A force. At many levels of truth this tests positive. Trying to separate it out and examine it apart from the other forces at work in our life and universe is, for most, a subjective experience. We can discuss many of the effects of Karmic Law and much more, but its inner mechanics remain a mystery. Trying to plot an "individual's" karmic patterns is almost ridiculous. No man can trace a "line of cause-and-effect", there is no such thing. No one typing at a computer keyboard can fully grasp its reality, though he may be working very well along its lines, perhaps unwittingly. It doesn't matter, we don't need to understand its inner workings, in the same way that you do not need to understand C++ to start your web browser. Although if the Law of Attraction was in your ken, you'd be better equipped to decipher all Karmic patterns, and much more besides.<br />
		<br />

		But we don't even need to know <em>that</em> to "do" karma yoga.<br />
		Okay, what <strong>do</strong> we need to know?<br />
		<br />

		<h4>Karma is a Law.</h4>
		Its mechanics were set in motion at the birth of our universe, no intervention is necessary. You cannot alter Karmic Law, stop trying. People get confused with their childlike notions of "sin" and "God", with "good" and "bad" , forgetting that this universe is governed by a set of definite Laws. Not some guy on a cloud. People wouldn't mess with Gravity like that, they just accept it, understand it, and then learn to work with it. In that order.<br />
		<br />

		Is making aeroplanes defying gravity?<br />
		<br />

		<h4>Karma Works, Perfectly</h4>
		It never fails. Ever. There is no such thing as "Good Karma" in the same way as there's no such thing as "good electricity", you could power life-support machines, or make Pop-Tarts, it's all the same stuff. That's as far as that analogy goes. You get what you need.<br />
		<br />

		<h4>Karma is INSTANT.</h4>
		It doesn't feck around. It's NOW. No one is blindly "paying for past wrongs" there is no such thing, grow up! Consider, it will be some time until you read this text, but for you it's RIGHT NOW!, and for me too! And further, it was created the very instant you requested it (oh yes you did). I just got round to writing it out just now, I've been very busy. Effects may take time to materialise in the physical world, yes, but the material world is only a small part of the picture, as you know. The cart follows the cow, to see all consequences of an action is Karmic mastery, or at least, mastery of Karmic seeing.<br />
		<br />

		<h4>It <em>really</em> Works.</h4>
		That is, at all times and in all places. For earthlings there is no "escape" from Karma in the same way as there is no escape from air. If you have lungs, you need air, very simple. If you are human, you need Karma. If you believe that someday soon you will suddenly be "released" from the wheel of cause and effect, you are mistaken, again. Comfort yourself with "Ignorance is a necessary prerequisite to knowledge", that's presuming that you can accept how wildly inaccurate your current Karmic Fantasies really are.<br />
		<br />

		<h4>It's "Everywhere"</h4>
		If you know what you are looking for you cannot fail to see the operations of Karmic Law working around you, in every event, from the smallest to the largest, from that blue feather blowing in the wind, to sky-scrapers smashing to the ground, all is within the framework of Karmic operation, every particle spins with Karma.<br />
		<br />

		<h4>You can Work with Karma</h4>
		Yes I know I said that you couldn't escape it, that doesn't mean you can't work with it, even work outside it, but that would take more practice and dedication than most folks are willing to provide. Fortunately, that's not necessary either, all one needs to do is work "with" Karma.<br />
		<br />

		..unless you are lucky.<br />
		<br />

		This is where we need to stop and take a breath. Then ask ourselves the big question-<br />
		<br />

		<h4>Do I believe in Luck?</h4>
		Take a minute with this one. Believing in luck and understanding Karmic Law aren't compatible, not in the same brain. You'll need to dump one. <br />
		<br />

		Okay, time for a pocket-description of Karma, (ten pages already and <em>now</em> he gives us the pocket description! ha! nothing's for nothin!) or as I like to call it, "Karmic Law"..<br />
		<br />

		<strong>Karmic Law is a universal principle operating to create ideal circumstances.</strong><br />
		<br />

		It's outside the scope of this piece, to delve into what those circumstances might be for any particular individual, or even what I mean by any of those words, because the topic here is "Karma Yoga" and so it's time for another pocket description..<br />
		<br />

		<em>"Karma", coming from the Sankrist word "Kri", means "to do", or "to act". "Yoga" is from "Yeug", meaning "a yoke", "To join";  "Union". "Karma Yoga" therefore means..</em><br />
		<br />


		<h1>to act in union</h1><br />

		You won't read a better description than that.<br />
		<br />

		In union with what? you might ask. At the most simplistic level we might say "With the Higher Will" and sure, many would attest to being "guided" into action, or paths of action along these lines. To the Yogi this is a satisfying response, and he would argue that this is the path of least resistance for the soul, offering swiftest results, and easiest progress. "Why not?"<br />
		<br />

		<h4>Or are we magus?</h4>
		This is all very well and good, and by all definitions, the individual who work's for works sake, is joyful in that work, sees himself a cog in the great machinery, seeks no reward, is attached to nothing and no one, yet gives love unending, etc, would be pretty well kitted out as a Trainee Karma Yogi. If he could add "joyfully guided" to his toolkit he'd be yogi bear status within weeks! but..<br />
		<br />

		<h4>ARE WE MAGUS?</h4>
		My own work in the field is concerned with what I have come to refer as "bending karma". With my pocket descriptions past, I can begin to share a little of the good stuff.<br />
		<br />

		It began with a simple question; "What if the laws of Karma, impartial as they are, could somehow be bent to one's own will? What if one's own Karma, and that of those around you, could be altered to one's own design?"<br />
		<br />

		A few lines from now I'll hit you with a small but very cute phrase. If you've followed me down to here then it might just make perfect sense. If you still haven't grasped the whole Karma=MC<sup>2</sup> thing, then you'll need to work some first.<br />
		<br />

		Do you believe you are being punished for past sins? If so, do begin again at the top of the page!  Do you believe that if you are a "good" person, you will be rewarded for this goodness? Please.. Line One! Do you still blame her for that? See above! Do you believe everything I've said you say? .. try again!<br />
		<br />

		You must accept that every single event in your life until this point has been for YOU! Perfect! Every word you've read, or heard, or even overheard, was FOR you? You see that? Do you see how the only thing which makes you angry is YOU DAMMIT!? Good, let's goan..<br />
		<br />

		I realized that Karmic Law never fails, that it could be the constant in any equation I chose to create. <strong>Karmic Law = <em>exactly what you need</em></strong>, so my initial experiments focused on a very simple tactic..<br />
		<br />

		<h4>Change what I need.</h4>
		This is a mindblowingly uncomplicated concept, yet, tremendously difficult for most adepts to at first understand, and even more difficult to implement wholeheartedly, simply because,<br />
		<br />

		a)  they don't yet know what it is they need (apart from a good kick up the arse) In other words, they don't "know themselves", or aren't "self-realized" or whatever your pet-phrase for this is, "got yoga" maybe? Perhaps their single-minded ambition and endless hunger is leaps and bounds for that soul, or another's listless vanity and dilettante occultist meanderings <em>are</em> in line with their evolution, maybe not, who can say? Can you?<br />
		<br />

		The point is, no one can work out an equation until they at least know <em>one</em> part of it! Might be best just to go with the flow for a while, watch how Karma works (start with small "events"); you may begin to recognise patterns, maybe you can read karmic data naturally(!), like the matrix, and just maybe you'll get some opportunities to work out that dreadful mess.<br />
		<br />

		and..<br />
		<br />

		b) Actually, I've forgotten what b) was going to be. Insert your own damned excuse!<br />
		<br />
		In short, it's gonna take a lot of work before you can understand karma yoga, because you'll first have to DO Karma yoga! If you would simply detach yourself from your actions, and their imagined results, you might truly experience the effects of Karmic Law working in a controlled, scientific manner (which your western brain MUST have, before it accepts the truth, yes?), and then even more work before you can start to experiment with the interwoven cause-and-effect mechanisms (let's call these babies "Karmix") not to mention your trials with the dynamics of the Law of Attraction.<br />
		<h3>Attraction</h3>
		Of course, you may find yourself in contact with a master who can quickly impart some reality into your sleepy brain cells.<br />
		<br />

		Maybe <em>then</em> you can start to bend karma.<br />
		<br />

		syat!<br />
		for now..<br />
