What is HTML, URL, Decimal Encoding/Decoding?<p>
<small>
In Web terms, Encoding allows for special characters to be escaped. For example,
for a Web browser to actually display the character '&lt;', it must be typed in the
Web page source as '&amp;lt;'.<br>
In other cases special characters should be represented with their hexadecimal
notations. Passing parameters on the URL line (known as query string) would be
one appropriate case. As an example, the character ':' would be represented as
'%3A'.<br>
In a Web page source all characters can be represented with their decimal 
notations. Browsers usually decode them to their equivalent characters before
displaying them. As an example, the character 'A' can be written as '&amp;#65;'
<p>

This page offers 6 different functions to encode or decode strings. they are:
<li>HTML Encode - HTML tags are converted to equivalent encoded format suitable to display them
exactly as they are without any further parsing. e.g. '&lt;' to '&amp;lt;'.</li>
<li>HTML Decode - An HTML encoded input is converted to its equivalent HTML representation with
the HTML tags restored. e.g. '&lt;' to '&amp;lt;'.</li>
<li>URL Encode - Special characters are converted to their hexadecimal notations, suitable
for usage in the URL, such as query string parameters. e.g. ':' to '%3A'. For URL
Encoding spaces are represented as '+'.</li>
<li>URL Decode - Encoded URL characters are converted to their actual representations. e.g. 
'%3A' to ':'.</li>
<li>Decimal Encode - Converts all characters to their decimal encoded HTML representation.
e.g. 'A' to '&amp;#65;'. (Best for ASCII characters)</li>
<li>Decimal Decode - Converts all decimal HTML codes to their original character representation.
e.g. '&amp;#65;' to 'A'. (Best for ASCII characters)</li>
</small>
