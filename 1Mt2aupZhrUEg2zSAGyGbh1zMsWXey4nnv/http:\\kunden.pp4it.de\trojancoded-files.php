      <h3 id="entschluesselung_1"><a href="#"><em>(P01.1)</em> Können Daten überhaupt gerettet werden?</a></h3>
      <div>
        <p>1. (Beispiel) locked-DATEINAME-Komische Dateiendung<br />
        Diese Variante kann in der Regel entschlüsselt werden. Dazu Bedarf es allerdings mindestens EINER Original Datei die NICHT verschlüsselt ist und exakt einer Datei die verschlüsselt wurde entspricht.<br />
        Woher diese saubere Datei kommt ist dabei natürlich egal. Sie kann aus einer Mail stammen, von einem USB-Stick, von einer CD/DVD, einer externen Festplatte, etc..</p>
        <p>2. (Beispiel) yLEAyojLAAnqjL<br />
        Diese Variante kann bislang (Stand: 07.06.2012 15:28 Uhr) MEINES Wissens nach NICHT wieder zurückkonvertiert werden.<br />
        Sämtliche Antivirenhersteller und sonstige Freien Leute die sich damit beschäftigen, arbeiten an einer Lösung. Wie es aber aussieht, wird mit Public-Keys und einer AES 128-Bit Verschlüsselung gearbeitet. Dies wiederum würde bedeuten das man OHNE die Beschlagnahmung der C&amp;C-Server (Command &amp; Control Server) KEINE Chance hätte diese Daten wieder zurückzuentschlüsseln.<br />
          Ein manuelles entschlüsseln würde alleine für wenige Dateien wohl 10tausende bist 100tausende Jahre dauern.<br />
          <br />
