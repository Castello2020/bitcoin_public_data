      <h3 id="explorer_verfuegbar"><a href="#"><em>(P02)</em> Was machen <span class="font_kapitel_std_rot">direkt</span> nachdem der Schädling wieder &quot;weg&quot; ist?</a></h3>
      <div>
        <p>Am besten wäre natürlich Daten sichern und Neuinstallieren, denn nur damit kann man sich eigentlich sicher sein dass das System wieder clean ist. <br />
