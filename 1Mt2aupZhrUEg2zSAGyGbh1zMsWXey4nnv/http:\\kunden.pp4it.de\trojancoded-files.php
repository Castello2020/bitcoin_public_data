        <p>Die unsichtbaren meist Systemdateien kann man recht einfach im Windows-Explorer unter dem Men&uuml;punkt <strong>Extras &gt; Ordneroptionen &gt; [Reiter] Ansicht</strong> unter den Punkten &quot;Gesch&uuml;tzte Systemdateien ausblenenden (empfohlen)&quot; und &quot;Ausgeblendete Dateien, Ordner und Laufwerke anzeigen&quot; aktivieren bzw. deaktivieren.</p>
      </div>
      <h3 id="systemwiederherstellung"><a href="#"><em>(P08)</em> Wie starte ich eine Systemwiederherstellung ohne lauffähiges Windows?</a></h3>
      <div>
        <p>Bei Windows 7 und Windows Vista ist dies sehr einfach. <br />
          Man lege die Installations-DVD ein und boote von dieser. Dann wählt man im ersten Men&uuml; die Sprache aus um dann einen Klick weiter die &quot;Computerreperaturoptionen&quot; anzuw&auml;hlen. Ab dort wird man durchgeleitet um einen fr&uuml;heren Systemwiederherstellungspunkt wieder herzustellen. </p>
        <p> Unter WindowsXP ist es noch etwas komplizierter. Man ben&ouml;tigt (soweit ich mich daran noch zur&uuml;ckerinnern kann *schmunzel*) noch Zugang zum System selbst, und wenn dies auch nur im Abgesicherten Modus wäre. Bekommt man diesen nicht, braucht man eine Boot-CD, wie z.B. einen ERD-Commander von Microsoft. (Diesen gibt es allerdings nur legal für MSDN-Kunden und fällt damit wohl für 98% aller Leute hier flach).</p>
      </div>
      <h3 id="woher"><a href="#"><em>(P09)</em> Woher habe ich diesen Mist?!</a></h3>
      <div>
        <p>Die Verteilung der Malware kommt in den meisten Fälle aktuell aber per Mail in angeblichen Rechnungen (siehe <a href="bka-entfernung-news.php" target="_blank">News</a>).</p>
        <p>In einigen Fällen wirst Du/werden Sie eine Seite besucht haben, welche schon seit Jahren besucht wurde und nie etwas geschehen ist. Nun aber ist zum Beispiel einer von hunderten Werbebannern infiziert gewesen und hat genau jetzt zugeschlagen. <br />
          Es gibt auch die Möglichkeit das sich im Hintergrund ein PopUp geöffnet hat und dies den Schädling aufgerufen hat. <br />
          Oder es wurde ein Link angeklickt weil zum Beispiel mit einem Browserspiel geworben wurde.</p>
        <p>Natürlich könnte man auch auf einer illegelan Webseite gewesen sein, bzw. mit nicht legalen Inhalten. Dann widerum geschieht es sogesehen (auch wenn das jetzt böse klingt) fast schon zurecht das man sich so etwas einfängt.</p>
        <p>Theoretisch wäre auch eine Verbreitung per CD/DVD, USB-Stick, USB-Festplatte, Netzwerk denkbar. Dies ist aber mir bislang noch nicht aufgefallen/untergekommen.      </p>
      </div>
      <h3 id="wieso"><a href="#"><em>(P10)</em> Ich habe doch einen AntiVirus/eine Firewall, wieso blockt das nicht?</a></h3>
      <div>
        <p>Eine genaue Erkl&auml;rung muss ich dabei Schuldig bleiben. Aber zumindest kommt diese Art von Sch&auml;dling bislang eigentlich ALLEN F&auml;llen die mir bekannt sind an wohl nicht aktuellem JAVA vorbei. Bis der Virenscanner dies erkannt hat, ist es dann leider zu sp&auml;t. <br/>
          Java lässt sich eigentlich ganz einfach per Besuch der Webseite <a href="http://www.java.com/"><code>http://www.java.com</code></a> aktuell halten. Noch einfacher ist das eigentlich immer wieder auftauchende Icon (orange) in der Taskleiste/Superbar bei der Systemuhr. <br/>
          Dort den Download best&auml;tigen und aktuell halten/bleiben. Der beste schutz gegen JAVA-Viren ist damit zumindest schon mal in Kraft. Nagut noch besser ist nat&uuml;rlich JAVA gar nicht auf dem System installiert zu haben. Da es aber h&auml;ufig gebraucht wird, ist das leider nicht in allen F&auml;llen m&ouml;glich.</p>
        <p>Zu JAVA gehört die <code>jusched.exe</code> welche im Autostart aktiviert sein sollte dazu. Diese Datei ist also, insofern sie nicht selbst infiziert wurde, eine ganz reguläre Datei und sollte nicht gelöscht werden.</p>
      </div>
      <h3 id="virenscanner_erkennung"><a href="#"><em>(P10.1)</em> Wieso kann ein (reiner) Virenscanner diese Malware nicht erkennen?</a></h3>
      <div>
        <p>Der Schädling selbst ist kein reiner Virus. Namentlich nennt sich solche Software  &ldquo;Malware&rdquo;.<br />
          Prinzipiell infiziert diese Software den Rechner und verschlüsselt alle Dateien die von Relevanz sein könntne für den User/die Userin.<br />
        </p>
        <p>Die   Virenscanner prüfen auf &ldquo;reine&rdquo; Viren nicht unbedingt aber auch auf    Änderungen an nicht Systemdateien.<br />
          Weiterhin muss auch keine Prüfung zu sehen sein, weil viele User/Userinnen als   Lokale Administrative Benutzer angemeldet sind (besonders bei Heimrechner).</p>
        <p>Entsprechend   kann ein Browser &amp; Betriebssystemhersteller machen was er will. Es   nützt nichts wenn ein User/eine Userin als lokaler Admin dran ist,   evtl. auch noch UAC (User Access Control unter WindowsVista/Windows7) runtergestellt oder gar ausgeschaltet   haben und dann noch JAVA für Browserspiele nutzen und dies dann nicht   aktuell halten.</p>
        <p>Die Verantwortung für solche Probleme liegt dann leider einzig und alleine beim User/bei der Userin.</p>
      </div>
      <h3 id="exe_fehler"><a href="#"><em>(P11)</em> Wäre bezahlen nicht einfacher?</a></h3>
      <div>
        <p>Klar wäre es das. Aber warum &quot;Verbrechern&quot; dieser Art auch noch Geld schenken? <br />
          Simple Antwort. Weil danach das Problem zu 99% immernoch herrschen würde. Sprich Geld für immer weg, Schädling und verschlüsselte weiterhin da! <br />
          Es gibt sogar Varianten solcher oder ähnlicher Schädlinge welche <b>NACH</b> dem Bezahlen sogar noch mehr Schädlinge runterladen und damit das System meist komplett lahmlegen und eine Neuinstallation unabdingbar machen.      </p>
      </div>
      <h3 id="anzeige"><a href="#"><em>(INFO)</em> Kann ich mich dagegen wehren? Zum Beispiel Anzeige erstatten?</a></h3>
