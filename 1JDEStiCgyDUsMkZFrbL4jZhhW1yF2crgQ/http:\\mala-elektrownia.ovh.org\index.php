<p>Ja zacznę...
...od masztu. Bo choć mogłoby wydawać się inaczej - to on jest
najważniejszą częścią wiatraka. Sam miałem okazję przekonać się o tym
gdy moje kochane psisko - Nikita, poradziła sobie z nim bez
najmniejszych problemów robiąc podkop nazbyt blisko kotew odciągu.
Bardzo piękna katastrofa była... Na ziemi legła dwutygodniowa praca.
Szczęście w tym, iż katastrofę przeżyła prądnica (stator i rotor) oraz
głowica. Resztę trzeba było zrobić ponownie, chociaż jak się później
okazało - bardzo dobrze, gdyż teraz wygląda to prawie profesjonalnie.
Poza tym zastosowałem kilka lżejszych materiałów takich jak aluminium,
czy wodoodporna sklejka odciążając samą głowicę. Po resztę informacji
odsyłam do odpowiednich zakładek...
</p>
